using Content.Shared.Administration;
using Robust.Client.AutoGenerated;
using Robust.Client.Console;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.Administration.UI.Tabs.AdminTab;

[GenerateTypedNameReferences]
public sealed partial class ScreenCheckAdminWindow : DefaultWindow
{
    [Dependency] private IClientConsoleHost _consoleHost = default!;

    private PlayerInfo? _selectedPlayer;

    public ScreenCheckAdminWindow()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        PlayerList.OnSelectionChanged += OnPlayerSelectionChanged;
        RequestScreenCheckButton.OnPressed += _ => OnScreenCheckPressed();
        OpenPlayerPanelButton.OnPressed += _ => OnOpenPlayerPanelPressed();

        HintLabel.Text = Loc.GetString("admin-screen-check-window-hint");
        RefreshUi();
    }

    private void OnPlayerSelectionChanged(PlayerInfo? selectedPlayer)
    {
        _selectedPlayer = selectedPlayer;
        RefreshUi();
    }

    private void RefreshUi()
    {
        if (_selectedPlayer == null)
        {
            SelectedPlayerLabel.Text = Loc.GetString("admin-screen-check-window-no-player");
            RequestScreenCheckButton.Disabled = true;
            OpenPlayerPanelButton.Disabled = true;
            return;
        }

        var statusKey = _selectedPlayer.Connected
            ? "admin-screen-check-window-status-online"
            : "admin-screen-check-window-status-offline";

        SelectedPlayerLabel.Text = Loc.GetString(
            "admin-screen-check-window-selected-player",
            ("player", _selectedPlayer.Username),
            ("status", Loc.GetString(statusKey)));

        RequestScreenCheckButton.Disabled = !_selectedPlayer.Connected;
        OpenPlayerPanelButton.Disabled = false;
    }

    private void OnScreenCheckPressed()
    {
        if (_selectedPlayer is not { Connected: true } player)
            return;

        _consoleHost.ExecuteCommand($"screencheck {player.Username}");
    }

    private void OnOpenPlayerPanelPressed()
    {
        if (_selectedPlayer == null)
            return;

        _consoleHost.ExecuteCommand($"playerpanel {_selectedPlayer.SessionId}");
    }
}
