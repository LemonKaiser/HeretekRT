    private static void UpdateChatControlPresentation(ChatControlPresentation presentation, bool reducedMotion)
    {
        if (reducedMotion)
            presentation.HoverAmount = presentation.Hovered ? 1f : 0f;

        var isChannel = presentation.Kind == ChatControlKind.Channel;
        var normalBackground = isChannel ? Color.FromHex("#15394CB8") : Color.FromHex("#120F0AD0");
        var hoverBackground = isChannel ? Color.FromHex("#1F506AD9") : Color.FromHex("#2A2111E8");
        var normalBorder = isChannel ? Color.FromHex("#4B7F9C8C") : Color.FromHex("#9E86504D");
        var hoverBorder = isChannel ? Color.FromHex("#90D2EE") : BrightGold;
        var normalText = isChannel ? Color.FromHex("#90D2EE") : Paper;
        var hoverText = isChannel ? Color.FromHex("#C6EEFF") : BrightGold;
        var pressedBackground = isChannel ? Color.FromHex("#285C76EF") : Color.FromHex("#4A3816E8");
        var pressedBorder = isChannel ? Color.FromHex("#D7F5FF") : Color.FromHex("#FFF1C8");

        var background = presentation.Pressed
            ? pressedBackground
            : LerpColor(normalBackground, hoverBackground, presentation.HoverAmount);
        var border = presentation.Pressed
            ? pressedBorder
            : LerpColor(normalBorder, hoverBorder, presentation.HoverAmount);
        var style = presentation.Button.StyleBoxOverride as LobbyRoundedStyleBox;
        if (style == null)
        {
            style = CreateChatControlStyle(background, border, cornerRadius: 3f);
            presentation.Button.StyleBoxOverride = style;
        }
        else
        {
            style.BackgroundColor = background;
            style.BorderColor = border;
        }
        presentation.Button.Label.FontColorOverride = LerpColor(normalText, hoverText, presentation.HoverAmount);
        presentation.VisualDirty = false;
    }

    public void SetChatExpanded(bool expanded)
    {
        _chatExpanded = expanded;

        if (expanded)
        {
            if (_chatPanelAvailable)
                RightSide.Visible = true;
        }
        else
        {
            Chat.ChatInput.Input.ReleaseKeyboardFocus();
        }

        if (IsReducedMotion)
            _chatCurrentWidth = expanded ? GetEffectiveChatWidth() : 0f;

        // Keep the drawer outside the screen from the very first frame. Otherwise its
        // XAML width is briefly visible before the animation gets its first update.
        UpdateChatPanelLayout();
    }

    public void UpdateChatAnimation(float deltaSeconds)
    {
        if (!_chatPanelAvailable)
            return;

        var targetWidth = _chatExpanded ? GetEffectiveChatWidth() : 0f;
        var panelWidthChanged = MathF.Abs(targetWidth - _lastChatPanelWidth) > 0.25f;
        if (IsReducedMotion)
        {
            if (MathF.Abs(_chatCurrentWidth - targetWidth) <= 0.01f && !panelWidthChanged)
                return;

            _chatCurrentWidth = targetWidth;
            UpdateChatPanelLayout();
            return;
        }

        // A closed, settled drawer has no visible controls to animate. Avoid repeatedly
        // assigning the same layout, visibility and modulation values every frame.
        if (!_chatExpanded && _chatCurrentWidth <= 0.01f)
        {
            _lastChatPanelWidth = targetWidth;
            return;
        }

        if (MathF.Abs(_chatCurrentWidth - targetWidth) <= 0.01f && !panelWidthChanged)
            return;

        var interpolation = Math.Clamp(deltaSeconds * ChatSlideSpeed, 0f, 1f);
        _chatCurrentWidth = MathHelper.Lerp(_chatCurrentWidth, targetWidth, interpolation);

        if (MathF.Abs(_chatCurrentWidth - targetWidth) < 0.25f)
            _chatCurrentWidth = targetWidth;

        UpdateChatPanelLayout();
    }

    private void UpdateChatPanelLayout()
    {
        var panelWidth = GetEffectiveChatWidth();
        _lastChatPanelWidth = panelWidth;
        var panelVisible = _chatExpanded || _chatCurrentWidth > 0f;
        RightSide.Visible = panelVisible;
        DrawerShadow.Visible = panelVisible;
        DrawerBlurBackdrop.Visible = panelVisible;
        if (!panelVisible)
        {
            ChatContent.Visible = false;
            RightSide.ModulateSelfOverride = null;
            DrawerShadow.ModulateSelfOverride = null;
            DrawerBlurBackdrop.ModulateSelfOverride = null;
            ChatContent.Modulate = Color.White;
            return;
        }

        // The chat itself keeps its natural minimum width.  Sliding the whole panel by
        // its right margin avoids a layout overflow that used to make it pop in at full size.
        RightSide.SetWidth = panelWidth;
        SetMarginRight(RightSide, panelWidth - _chatCurrentWidth);
        DrawerBlurBackdrop.SetWidth = panelWidth;
        SetMarginRight(DrawerBlurBackdrop, panelWidth - _chatCurrentWidth);
        DrawerBlurBackdrop.BackgroundTexture = Background.Texture;
        DrawerBlurBackdrop.BackgroundPixelSize = Background.PixelSize;
        DrawerBlurBackdrop.BackgroundGlobalPixelPosition = Background.GlobalPixelPosition;
        // On compact screens the drawer owns the whole viewport, so a separate
        // left-edge shadow would only create an artificial dark strip.
        DrawerShadow.SetWidth = IsFullscreenChatLayout() ? panelWidth : panelWidth + 64f;
        DrawerShadow.DrawerWidth = panelWidth;
        SetMarginRight(DrawerShadow, panelWidth - _chatCurrentWidth);
        var openAmount = panelWidth <= 0f
            ? 0f
            : Math.Clamp(_chatCurrentWidth / panelWidth, 0f, 1f);
        var easedOpenAmount = openAmount * openAmount * (3f - 2f * openAmount);
        // The drawer surface starts softly before text becomes readable; this avoids
        // the previous hard pop when ChatContent crossed a width threshold.
        RightSide.ModulateSelfOverride = Color.White.WithAlpha(0.12f + easedOpenAmount * 0.88f);
        DrawerBlurBackdrop.ModulateSelfOverride = Color.White.WithAlpha(0.12f + easedOpenAmount * 0.88f);
        DrawerShadow.Opacity = easedOpenAmount;
        DrawerShadow.ModulateSelfOverride = Color.White.WithAlpha(easedOpenAmount);
        ChatContent.Visible = _chatCurrentWidth >= ChatContentRevealThreshold * 0.5f;
        ChatContent.Modulate = Color.White.WithAlpha(easedOpenAmount);
    }

    private float GetEffectiveChatWidth()
    {
        var viewportWidth = PixelSize.X / MathF.Max(UIScale, 0.01f);
        if (viewportWidth <= 0f)
            return DesktopChatWidth;

        // Mirrors the HTML prototype: the compact layout takes the complete
        // viewport, while the desktop drawer never exceeds its designed width.
        if (viewportWidth <= CompactChatBreakpoint)
            return viewportWidth;

        return Math.Min(DesktopChatWidth, viewportWidth - DesktopChatViewportInset);
    }

    private bool IsFullscreenChatLayout()
    {
        var viewportWidth = PixelSize.X / MathF.Max(UIScale, 0.01f);
        return viewportWidth > 0f && viewportWidth <= CompactChatBreakpoint;
    }

    private bool IsReducedMotion => _cfg.GetCVar(CCVars.ReducedMotion);

    private void ApplyStaticVisuals()
    {
        LogoDisplay.Pulse = 0f;
        DeckDecoration.AnimationTime = 0f;
        RoundIndicator.Pulse = 0f;
        StatusBlock.Modulate = Color.White;
        MenuBlock.Modulate = Color.White;
        BottomInfoPanel.Modulate = Color.White;
        StatusBlock.Margin = new Thickness(0f, 0f, 0f, 16f);
        MenuBlock.Margin = new Thickness(0f);
        BottomInfoPanel.Margin = new Thickness(33f, 14f, 0f, 0f);
        SetMarginLeft(MainContainer, _responsiveLeftInset);
        SetMarginTop(UtilityBar, 20);

        if (_lastReadyPulse != 0f)
            ApplyReadyButtonVisual(_ready, _roundStarted, pulse: 0f);
    }

    private void ApplyIntroOpacity(float left, float utility, float chat)
    {
        // Modulate is inherited by child controls; ModulateSelfOverride would only fade the
        // empty containers and leave their labels/buttons visible.
        LeftSide.Modulate = Color.White.WithAlpha(left);
        UtilityBar.Modulate = Color.White.WithAlpha(utility);
        RightSide.Modulate = Color.White.WithAlpha(chat);
        var status = EaseTimeline(_introElapsed, 0.61f, 0.42f);
        var menu = EaseTimeline(_introElapsed, 0.76f, 0.48f);
        var footnote = EaseTimeline(_introElapsed, 1.02f, 0.36f);
        StatusBlock.Modulate = Color.White.WithAlpha(status);
        MenuBlock.Modulate = Color.White.WithAlpha(menu);
        BottomInfoPanel.Modulate = Color.White.WithAlpha(footnote);
        // Mirror the prototype's small upward arrival without translating the whole
        // command deck or changing its resting geometry.
        StatusBlock.Margin = new Thickness(0f, 13f * (1f - status), 0f, 16f);
        MenuBlock.Margin = new Thickness(0f, 13f * (1f - menu), 0f, 0f);
        BottomInfoPanel.Margin = new Thickness(33f, 14f + 13f * (1f - footnote), 0f, 0f);
        SetMarginLeft(MainContainer, MathF.Max(0f, _responsiveLeftInset - 32f * (1f - left)));
        SetMarginTop(UtilityBar, 7f + 13f * utility);
    }

    private void UpdateResponsiveLayout()
    {
        var viewportWidth = PixelSize.X / MathF.Max(UIScale, 0.01f);
        if (viewportWidth <= 1f || MathF.Abs(viewportWidth - _lastResponsiveViewportWidth) < 0.5f)
            return;

        _lastResponsiveViewportWidth = viewportWidth;
        _responsiveDeckWidth = Math.Clamp(viewportWidth - 48f, 300f, 440f);
        _responsiveLeftInset = Math.Clamp(viewportWidth * 0.07f, 24f, 126f);
        _responsiveLeftInset = Math.Min(
            _responsiveLeftInset,
            MathF.Max(16f, viewportWidth - _responsiveDeckWidth - 16f));

        MainContainer.SetWidth = _responsiveDeckWidth;
        LeftSide.SetWidth = _responsiveDeckWidth;
        if (_introActive)
            return;

        SetMarginLeft(MainContainer, _responsiveLeftInset);
    }

    private static float EaseTimeline(float elapsed, float start, float duration)
    {
        var progress = Math.Clamp((elapsed - start) / duration, 0f, 1f);
        return progress * progress * (3f - 2f * progress);
    }

    private void SetChatPanelAvailable(bool available)
    {
        _chatPanelAvailable = available;
        UtilityBar.Visible = available;

        if (!available)
        {
            RightSide.Visible = false;
            DrawerShadow.Visible = false;
            DrawerBlurBackdrop.Visible = false;
            ChatContent.Visible = false;
            return;
        }

        UpdateChatPanelLayout();
    }

    public void SwitchState(LobbyGuiState state)
    {
        DefaultState.Visible = false;
        CharacterSetupState.Visible = false;
        MainContainer.Visible = false;

        switch (state)
        {
            case LobbyGuiState.Default:
                MainContainer.Visible = true;
                DefaultState.Visible = true;
                SetChatPanelAvailable(true);
                break;
            case LobbyGuiState.CharacterSetup:
                CharacterSetupState.Visible = true;
                SetChatPanelAvailable(false);

                UserInterfaceManager.GetUIController<LobbyUIController>().ReloadCharacterSetup();
                break;
        }
    }

    public void ApplyPanelBackgroundOpacity(float opacity)
    {
        LeftSideTop.PanelOverride = LobbyPanelOpacityHelper.CreateOpacityStyle(_leftSideTopPanelStyle, opacity);
        BottomInfoPanel.PanelOverride = LobbyPanelOpacityHelper.CreateOpacityStyle(_bottomInfoPanelStyle, opacity);
        // The drawer is its own glass surface, not an ordinary lobby card. Applying
        // the global panel-opacity slider here makes the chat unreadable on bright art.
        RightSide.PanelOverride = _rightSidePanelStyle;
    }

