    private void ConfigureActionHover(Button button, PanelContainer row)
    {
        var presentation = new MenuRowPresentation(button, row);
        _menuRows.Add(presentation);

        // The numeric column belongs to the row surface too, while the real button
        // retains clicks. Hover itself is sampled from the UI manager every frame:
        // storing separate enter/exit flags for a parent and a child can leave a
        // surface lit when the pointer moves between their bounds.
        row.MouseFilter = MouseFilterMode.Pass;
        row.DefaultCursorShape = Control.CursorShape.Hand;
        button.OnButtonDown += _ =>
        {
            presentation.Pressed = true;
            presentation.VisualDirty = true;
        };
        button.OnButtonUp += _ =>
        {
            presentation.Pressed = false;
            presentation.VisualDirty = true;
        };

        UpdateMenuRowPresentation(presentation, reducedMotion: true);
    }

