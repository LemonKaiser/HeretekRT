    private static void ApplyUtilityButtonVisual(Button button, float hoverAmount, bool pressed)
    {
        hoverAmount = Math.Clamp(hoverAmount, 0f, 1f);
        var background = pressed
            ? Color.FromHex("#4A3816E6")
            : LerpColor(Color.FromHex("#0C0C0A8A"), Color.FromHex("#272114D6"), hoverAmount);
        var border = pressed
            ? Color.FromHex("#FFF1C8")
            : LerpColor(GoldBorder, BrightGold, hoverAmount);
        var style = button.StyleBoxOverride as LobbyRoundedStyleBox;
        if (style == null)
        {
            style = CreateUtilityStyleBox(background, border, cornerRadius: 10f);
            button.StyleBoxOverride = style;
        }
        else
        {
            style.BackgroundColor = background;
            style.BorderColor = border;
        }
        button.Label.FontColorOverride = LerpColor(Paper, BrightGold, hoverAmount);
        // Keep the allocated slot fixed: only the button itself rises, never the
        // separator or its neighbours in the top utility bar.
        button.Margin = new Thickness(0f, -2f * hoverAmount, 0f, 2f * hoverAmount);
    }

    private TextureRect AddUtilityTexture(Button button, string texturePath)
