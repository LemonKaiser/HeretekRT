        ApplyReadyRowVisual(ready, roundStarted: false, pulse);
        _lastReadyPulse = pulse;
    }

    /// <summary>
    /// Starts the lobby presentation once for the lifetime of the client process.
    /// Returning to the lobby uses the normal immediately-visible layout.
    /// </summary>
    public void BeginPresentation()
    {
        _introElapsed = 0f;
        _ambientElapsed = 0f;
        _introActive = !_introPlayedThisProcess && !IsReducedMotion;
        _introPlayedThisProcess = true;

        if (_introActive)
        {
            ApplyIntroOpacity(left: 0f, utility: 0f, chat: 0f);
            return;
        }

        ApplyIntroOpacity(left: 1f, utility: 1f, chat: 1f);
        ApplyStaticVisuals();
    }

    public void UpdateVisualEffects(float deltaSeconds)
    {
        UpdateResponsiveLayout();
        UpdateInteractiveVisuals(deltaSeconds, IsReducedMotion);

        if (IsReducedMotion)
        {
            _introActive = false;
            ApplyIntroOpacity(left: 1f, utility: 1f, chat: 1f);
            ApplyStaticVisuals();
            return;
        }

        _ambientElapsed += deltaSeconds;

        if (_introActive)
        {
            _introElapsed += deltaSeconds;
            ApplyIntroOpacity(
                left: EaseTimeline(_introElapsed, 0.05f, 0.52f),
                utility: EaseTimeline(_introElapsed, 0.42f, 0.42f),
                chat: EaseTimeline(_introElapsed, 0.58f, 0.47f));

            if (_introElapsed >= IntroDuration)
            {
                _introActive = false;
                ApplyIntroOpacity(left: 1f, utility: 1f, chat: 1f);
            }
        }

        var logoPulse = 0.5f + 0.5f * MathF.Sin(_ambientElapsed * MathF.Tau / LogoGlowCycleDuration);
        LogoDisplay.Pulse = logoPulse;
        DeckDecoration.AnimationTime = _ambientElapsed;
        var signalPhase = _ambientElapsed / 2.4f;
        signalPhase -= MathF.Floor(signalPhase);
        RoundIndicator.Pulse = signalPhase;
        _drawerDecoration.Pulse = logoPulse;
        if (_roundStarted)
            return;

        var readyPulse = 0.5f + 0.5f * MathF.Sin(_ambientElapsed * MathF.Tau / ReadyPulseCycleDuration);
        if (MathF.Abs(readyPulse - _lastReadyPulse) >= 0.015f)
            ApplyReadyButtonVisual(_ready, roundStarted: false, readyPulse);
    }

    private void UpdateInteractiveVisuals(float deltaSeconds, bool reducedMotion)
    {
        foreach (var presentation in _menuRows)
        {
            UpdateMenuRowHoverState(presentation);
            var previous = presentation.HoverAmount;
            presentation.HoverAmount = ApproachPresentation(
                presentation.HoverAmount,
                presentation.Hovered ? 1f : 0f,
                deltaSeconds,
                reducedMotion);

            if (presentation.HoverAmount == previous && !presentation.VisualDirty)
                continue;

            UpdateMenuRowPresentation(presentation, reducedMotion: false);
        }

        UpdateReadyHoverState();
        var readyPrevious = _readyHoverAmount;
        _readyHoverAmount = ApproachPresentation(
            _readyHoverAmount,
            _readyHovered ? 1f : 0f,
            deltaSeconds,
            reducedMotion);
        if (_readyHoverAmount != readyPrevious || _readyVisualDirty)
        {
            ReadyButton.Label.Margin = new Thickness(8f * _readyHoverAmount, 0f, 0f, 0f);
            ApplyReadyButtonVisual(_ready, _roundStarted, _lastReadyPulse < 0f ? 0f : _lastReadyPulse);
            _readyVisualDirty = false;
        }

        foreach (var presentation in _utilityButtons)
        {
            var previous = presentation.HoverAmount;
            presentation.HoverAmount = ApproachPresentation(
                presentation.HoverAmount,
                presentation.Hovered ? 1f : 0f,
                deltaSeconds,
                reducedMotion);

            if (presentation.HoverAmount == previous && !presentation.VisualDirty)
                continue;

            UpdateUtilityPresentation(presentation, reducedMotion: false);
        }

        foreach (var presentation in _communityButtons)
        {
            var previous = presentation.HoverAmount;
            presentation.HoverAmount = ApproachPresentation(
                presentation.HoverAmount,
                presentation.Hovered ? 1f : 0f,
                deltaSeconds,
                reducedMotion);

            if (presentation.HoverAmount == previous && !presentation.VisualDirty)
                continue;

            UpdateCommunityPresentation(presentation, reducedMotion: false);
        }

        var authorsPrevious = _authorsHoverAmount;
        _authorsHoverAmount = ApproachPresentation(
            _authorsHoverAmount,
            _authorsHovered ? 1f : 0f,
            deltaSeconds,
            reducedMotion);
        if (_authorsHoverAmount != authorsPrevious || _authorsVisualDirty)
        {
            UpdateAuthorsButtonVisual();
            _authorsVisualDirty = false;
        }

        foreach (var presentation in _chatControls)
        {
            var previous = presentation.HoverAmount;
            presentation.HoverAmount = ApproachPresentation(
                presentation.HoverAmount,
                presentation.Hovered ? 1f : 0f,
                deltaSeconds,
                reducedMotion);

            if (presentation.HoverAmount == previous && !presentation.VisualDirty)
                continue;

            UpdateChatControlPresentation(presentation, reducedMotion: false);
        }
    }

    private static float ApproachPresentation(float value, float target, float deltaSeconds, bool reducedMotion)
    {
        if (reducedMotion)
            return target;

        var speed = Math.Clamp(deltaSeconds * 12f, 0f, 1f);
        var approached = value + (target - value) * speed;
        return MathF.Abs(approached - target) <= 0.002f ? target : approached;
    }

    private static Color LerpColor(Color from, Color to, float progress)
    {
        progress = Math.Clamp(progress, 0f, 1f);
        return new Color(
            from.R + (to.R - from.R) * progress,
            from.G + (to.G - from.G) * progress,
            from.B + (to.B - from.B) * progress,
            from.A + (to.A - from.A) * progress);
    }

    private static void UpdateMenuRowPresentation(MenuRowPresentation presentation, bool reducedMotion)
    {
        if (presentation.Button.Disabled)
        {
            presentation.HoverAmount = 0f;
            presentation.Pressed = false;
            presentation.Button.Label.Margin = default;
            presentation.Button.Label.FontColorOverride = Color.FromHex("#8A806D");
            ApplyStandardRowVisual(presentation.Row, hovered: false);
            presentation.VisualDirty = false;
            return;
        }

        if (reducedMotion)
            presentation.HoverAmount = presentation.Hovered ? 1f : 0f;

        presentation.Button.Label.Margin = new Thickness(8f * presentation.HoverAmount, 0f, 0f, 0f);
        presentation.Button.Label.FontColorOverride = LerpColor(Paper, Color.FromHex("#FFF4D9"), presentation.HoverAmount);
        ApplyStandardRowVisual(presentation.Row, presentation.HoverAmount, presentation.Pressed);
        presentation.VisualDirty = false;
    }

    private static void UpdateUtilityPresentation(UtilityPresentation presentation, bool reducedMotion)
    {
        if (reducedMotion)
            presentation.HoverAmount = presentation.Hovered ? 1f : 0f;

        ApplyUtilityButtonVisual(presentation.Button, presentation.HoverAmount, presentation.Pressed);
        var tint = presentation.Pressed
            ? Color.FromHex("#FFF1C8")
            : LerpColor(Paper, BrightGold, presentation.HoverAmount);
        presentation.SetIconTint(tint);
        presentation.VisualDirty = false;
    }

    private static void UpdateCommunityPresentation(CommunityPresentation presentation, bool reducedMotion)
    {
        if (reducedMotion)
            presentation.HoverAmount = presentation.Hovered ? 1f : 0f;

        var background = presentation.Pressed
            ? Color.FromHex("#A8793D94")
            : LerpColor(Color.FromHex("#17140DC7"), Color.FromHex("#43361CD9"), presentation.HoverAmount);
        var border = presentation.Pressed
            ? Color.FromHex("#FFF1C8")
            : LerpColor(Color.FromHex("#DFD7C38A"), BrightGold, presentation.HoverAmount);
        var style = presentation.Button.StyleBoxOverride as LobbyRoundedStyleBox;
        if (style == null)
        {
            style = CreateCommunityStyleBox(background, border);
            presentation.Button.StyleBoxOverride = style;
        }
        else
        {
            style.BackgroundColor = background;
            style.BorderColor = border;
        }
        presentation.Icon.ModulateSelfOverride = presentation.Pressed
            ? Color.FromHex("#FFF1C8")
            : LerpColor(Paper, BrightGold, presentation.HoverAmount);
        presentation.Button.Margin = new Thickness(0f, -2f * presentation.HoverAmount, 0f, 2f * presentation.HoverAmount);
        presentation.VisualDirty = false;
    }

    private void UpdateAuthorsButtonVisual()
