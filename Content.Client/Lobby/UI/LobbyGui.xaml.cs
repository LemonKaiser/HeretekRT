        ApplyStandardRowVisual(row, hovered ? 1f : 0f, pressed);
    }

    private static void ApplyStandardRowVisual(PanelContainer row, float hoverAmount, bool pressed = false)
    {
        hoverAmount = Math.Clamp(hoverAmount, 0f, 1f);
        var normal = Color.Transparent;
        var hovered = pressed
            ? Color.FromHex("#7B5E214D")
            : Color.FromHex("#684C1B4F");
        var style = GetMenuRowStyle(row);
        style.LeftColor = LerpColor(normal, hovered, hoverAmount);
        style.MiddleColor = Color.Transparent;
        style.RightColor = Color.FromHex("#34271100");
        style.MiddleStop = 0f;
        style.AccentColor = BrightGold.WithAlpha((pressed ? 1f : 0.78f) * hoverAmount);
        style.DrawAccent = hoverAmount > 0.01f || pressed;
        style.DrawDivider = true;
    }

    private void ApplyReadyRowVisual(bool ready, bool roundStarted, float pulse)
    {
        if (roundStarted)
        {
            ApplyStandardRowVisual(ReadyRow, _readyHoverAmount, _readyPressed);
            ReadyIndex.FontColorOverride = Gold;
            return;
        }

        var intensity = 0.64f + 0.30f * pulse + _readyHoverAmount * 0.06f;
        var middleIntensity = 0.50f + 0.13f * pulse + _readyHoverAmount * 0.06f;
        var baseLeft = ready ? Color.FromHex("#845D18") : Color.FromHex("#80261D");
        var hoverLeft = ready ? Color.FromHex("#A57620") : Color.FromHex("#9D3124");
        var left = LerpColor(baseLeft, hoverLeft, _readyHoverAmount).WithAlpha(intensity);
        var baseMiddle = ready ? Color.FromHex("#684812") : Color.FromHex("#711E18");
        var hoverMiddle = ready ? Color.FromHex("#8A601A") : Color.FromHex("#81251F");
        var middle = LerpColor(baseMiddle, hoverMiddle, _readyHoverAmount).WithAlpha(middleIntensity);
        var style = GetMenuRowStyle(ReadyRow);
        style.LeftColor = left;
        style.MiddleColor = middle;
        style.RightColor = middle.WithAlpha(0f);
        style.MiddleStop = 0.72f;
        style.AccentColor = ready ? BrightGold : Color.FromHex("#FF906D");
        style.DrawAccent = true;
        style.DrawDivider = false;
        ReadyIndex.FontColorOverride = ready ? BrightGold : Color.FromHex("#FFB080");
    }

    private static LobbyMenuRowStyleBox GetMenuRowStyle(PanelContainer row)
    {
        if (row.PanelOverride is LobbyMenuRowStyleBox style)
            return style;

        style = new LobbyMenuRowStyleBox();
        row.PanelOverride = style;
        return style;
    }

