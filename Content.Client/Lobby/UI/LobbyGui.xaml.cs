    private void ApplyReadyButtonVisual(bool ready, bool roundStarted, float pulse)
    {
        if (roundStarted)
        {
            ConfigureActionButton(ReadyButton);
            ReadyButton.Label.FontColorOverride = Paper;
            ApplyReadyRowVisual(ready, roundStarted: true, pulse: 0f);
