    private void SetNavigationMode(bool bssMode)
    {
        _bssMode = bssMode;
        NavSubModeButton.Pressed = !bssMode;
        BssSubModeButton.Pressed = bssMode;
        NavModeControls.Visible = !bssMode;
        BssModeControls.Visible = bssMode;
        CancelTargeting();

        if (bssMode && _shuttleEntity != null &&
            _entManager.TryGetComponent(_shuttleEntity.Value, out TransformComponent? shuttleXform) &&
            shuttleXform.MapID != _bssObjectMap)
        {
            _bssObjectsDirty = true;
        }

        if (bssMode && _bssObjectsDirty)
            RebuildBssObjects();
    }

    private void SetTargeting(bool pressed, ShuttleNavTargetingMode mode)
    {
        if (_navigationSuppressed || pressed && mode == ShuttleNavTargetingMode.Bss && _ftlState != FTLState.Available)
        {
            CancelTargeting();
            return;
        }

        if (!pressed)
        {
            CancelTargeting();
            return;
        }

        NavAutopilotButton.Pressed = mode == ShuttleNavTargetingMode.Autopilot;
        BssFTLButton.Pressed = mode == ShuttleNavTargetingMode.Bss;
        NavRadar.SetTargetingMode(mode);
        ClosePlanetaryDetails();
    }

    private void CancelTargeting()
    {
        NavAutopilotButton.Pressed = false;
        BssFTLButton.Pressed = false;
        NavRadar.SetTargetingMode(ShuttleNavTargetingMode.None);
    }

    private void OnNavigationTargetSelected(EntityCoordinates coordinates)
    {
        if (_navigationSuppressed || NavRadar.TargetingMode == ShuttleNavTargetingMode.None)
            return;

        var targetAngle = NavRadar.TargetAngle;
        if (NavRadar.TargetingMode == ShuttleNavTargetingMode.Autopilot)
        {
            if (NavRadar.TryGetTargetGrid(coordinates, out var targetGrid))
                RequestAutopilotGrid?.Invoke(_entManager.GetNetEntity(targetGrid));
            else
                RequestAutopilot?.Invoke(_xformSystem.ToMapCoordinates(coordinates), targetAngle);
        }
        else if (NavRadar.TryGetTargetBeacon(coordinates, out var targetBeacon))
        {
            RequestBeaconFTL?.Invoke(targetBeacon.Entity, targetAngle);
        }
        else
        {
            RequestFTL?.Invoke(_xformSystem.ToMapCoordinates(coordinates), targetAngle);
        }

        CancelTargeting();
    }

    private void ScanBssObjects()
    {
        if (_navigationSuppressed)
            return;

        if (_consoleEntity != null)
        {
            _audio.PlayEntity(
                new SoundPathSpecifier("/Audio/Effects/Shuttle/radar_ping.ogg"),
                Filter.Local(),
                _consoleEntity.Value,
                true);
        }

        _bssObjectsDirty = true;
        RebuildBssObjects();
        _nextBssScan = _timing.CurTime + BssScanCooldown;
        BssRebuildButton.Disabled = true;
    }

    private void RebuildBssObjects()
    {
        BssDestinations.DisposeAllChildren();
        _bssObjectsDirty = false;
        if (_navigationSuppressed || _shuttleEntity == null ||
            !_entManager.TryGetComponent(_shuttleEntity.Value, out TransformComponent? shuttleXform) ||
            shuttleXform.MapID == MapId.Nullspace)
        {
            return;
        }

        var mapId = shuttleXform.MapID;
        _bssObjectMap = mapId;
        var mapUid = _mapManager.GetMapEntityId(mapId);
        var mapName = _entManager.TryGetComponent(mapUid, out MetaDataComponent? metadata) &&
                      !string.IsNullOrWhiteSpace(metadata.EntityName)
            ? metadata.EntityName
            : Loc.GetString("shuttle-console-unknown");

        var heading = new CollapsibleHeading(mapName)
        {
            MinHeight = 32f,
            HorizontalAlignment = HAlignment.Stretch,
            HorizontalExpand = true,
        };
        heading.AddStyleClass(ContainerButton.StyleClassButton);
        heading.Label.HorizontalAlignment = HAlignment.Center;
        heading.Label.HorizontalExpand = true;

        var objectContainer = new BoxContainer
        {
            Orientation = LayoutOrientation.Vertical,
            HorizontalExpand = true,
        };
        var entries = new List<IMapObject>();
        foreach (var grid in _mapManager.GetAllGrids(mapId))
        {
            _entManager.TryGetComponent(grid.Owner, out IFFComponent? iff);
            var hideLabel = iff != null &&
                            (iff.Flags & IFFFlags.HideLabel) != 0 &&
                            grid.Owner != _shuttleEntity;
            var detection = _consoleEntity == null
                ? DetectionLevel.Detected
                : _detection.IsGridDetected(grid.Owner, _consoleEntity.Value);
            if (detection == DetectionLevel.Undetected && hideLabel || iff != null && (iff.Flags & IFFFlags.Hide) != 0)
                continue;

            var name = hideLabel
                ? detection == DetectionLevel.PartialDetected
                    ? Loc.GetString("shuttle-console-signature-infrared")
                    : _detection.HandleUnknownMassLabel(grid.Owner)
                : _entManager.GetComponent<MetaDataComponent>(grid.Owner).EntityName;
            if (iff != null && (iff.Flags & IFFFlags.HideLabelAlways) != 0)
                continue;

            entries.Add(new GridMapObject
            {
                Name = name,
                Entity = grid.Owner,
            });
        }

        foreach (var (exclusion, _) in _shuttles.GetExclusions(mapId, _bssExclusions))
        {
            if (!exclusion.HideButton)
                entries.Add(exclusion);
        }

        foreach (var (beacon, _) in _shuttles.GetBeacons(mapId, _bssBeacons))
        {
            if (!beacon.HideButton)
                entries.Add(beacon);
        }

        entries.Sort((left, right) => string.Compare(left.Name, right.Name, StringComparison.CurrentCultureIgnoreCase));
        foreach (var entry in entries)
        {
            var button = new Button
            {
                Text = entry.Name,
                HorizontalExpand = true,
                StyleClasses = { "ButtonSquare" },
            };
            button.Label.ClipText = true;
            button.ToolTip = entry.Name;
            button.OnPressed += _ => NavRadar.CenterOn(_shuttles.GetMapCoordinates(entry));
            objectContainer.AddChild(button);
        }

        var body = new CollapsibleBody
        {
            HorizontalAlignment = HAlignment.Stretch,
            HorizontalExpand = true,
            Children = { objectContainer },
        };
        var collapsible = new Collapsible(heading, body)
        {
            BodyVisible = true,
        };
        BssDestinations.AddChild(collapsible);
    }

    // Frontier - IFF search
    private void OnIffSearchChanged(string text)
    {
        text = text.Trim();

        NavRadar.IFFFilter = text.Length == 0
            ? null // If empty, do not filter
            : (entity, grid, iff, hideLabel, name) => // Otherwise use simple search criteria
            {
                // Check entity name
                if (name.Contains(text, StringComparison.OrdinalIgnoreCase))
                {
                    return true;
                }
                if (hideLabel)
                    return false;

                // Check company name
                if (_entManager.TryGetComponent<CompanyComponent>(entity, out var companyComp) &&
                    !string.IsNullOrEmpty(companyComp.CompanyName))
                {
                    // Try to match the company ID directly
                    if (companyComp.CompanyName.Id.Contains(text, StringComparison.OrdinalIgnoreCase))
                    {
                        return true;
                    }

                    // Try to match company name from prototype
                    if (_prototypeManager.TryIndex(
                        companyComp.CompanyName, out var prototype) &&
                        prototype.Name.Contains(text, StringComparison.OrdinalIgnoreCase))
                    {
                        return true;
                    }
                }

                return false;
            };
    }

