    public void UpdateMapState(ShuttleMapInterfaceState state)
    {
        var objectsChanged = !_bssBeacons.SequenceEqual(state.Destinations) ||
                             !_bssExclusions.SequenceEqual(state.Exclusions);
        var previousState = _ftlState;
        _ftlState = state.FTLState;
        _ftlTime = state.FTLTime;
        _bssBeacons = state.Destinations;
        _bssExclusions = state.Exclusions;
        _bssObjectsDirty |= objectsChanged;
        if (previousState != _ftlState && _ftlState is FTLState.Available or FTLState.Cooldown)
            _bssObjectsDirty = true;
        NavRadar.SetBssObjects(_bssBeacons, _bssExclusions);
        BssFTLState.Text = Loc.GetString($"shuttle-console-ftl-state-{_ftlState}");

        switch (_ftlState)
        {
            case FTLState.Available:
                _ftlStyle.BackgroundColor = Color.FromHex("#80C71F");
                break;
            case FTLState.Starting:
                _ftlStyle.BackgroundColor = Color.FromHex("#169C9C");
                break;
            case FTLState.Travelling:
                _ftlStyle.BackgroundColor = Color.FromHex("#8932B8");
                break;
            case FTLState.Arriving:
                _ftlStyle.BackgroundColor = Color.FromHex("#F9801D");
                break;
            default:
                _ftlStyle.BackgroundColor = Color.FromHex("#B02E26");
                break;
        }

        BssFTLButton.Disabled = _navigationSuppressed || _ftlState != FTLState.Available;
        BssRebuildButton.Disabled = _navigationSuppressed || _ftlState is FTLState.Starting or FTLState.Travelling or FTLState.Arriving;
        NavAutopilotButton.Disabled = _navigationSuppressed;
        if (BssFTLButton.Disabled && NavRadar.TargetingMode == ShuttleNavTargetingMode.Bss)
            CancelTargeting();

        if (_bssMode && _bssObjectsDirty)
            RebuildBssObjects();
    }

    public void UpdatePlanetaryState(KoronusPlanetaryInterfaceState state)
    {
        _planetaryState = state;
        _navigationSuppressed = state.NavigationSuppressed;
        NavRadar.UpdatePlanetaryState(state);

        NavAutopilotButton.Disabled = _navigationSuppressed;
        BssFTLButton.Disabled = _navigationSuppressed || _ftlState != FTLState.Available;
        BssRebuildButton.Disabled = _navigationSuppressed || _ftlState is FTLState.Starting or FTLState.Travelling or FTLState.Arriving;
        if (_navigationSuppressed)
            CancelTargeting();

        if (!state.Available)
        {
            _selectedPlanetaryBody = null;
            _selectedLandingSite = null;
            PlanetaryDetailsPanel.Visible = false;
            NavRadar.Visible = true;
            PlanetaryNoSignal.Visible = false;
            return;
        }

        NavRadar.Visible = !state.NavigationSuppressed;
        PlanetaryNoSignal.Visible = state.NavigationSuppressed;

        if (_selectedPlanetaryBody != null && state.Bodies.All(body => body.Id != _selectedPlanetaryBody))
        {
            _selectedPlanetaryBody = null;
            _selectedLandingSite = null;
            PlanetaryDetailsPanel.Visible = false;
        }

        if (state.LandedBody != null)
        {
            _selectedPlanetaryBody = state.LandedBody;
            var landedBody = state.Bodies.FirstOrDefault(body => body.Id == state.LandedBody);
            _selectedLandingSite = landedBody?.LandingSites
                .FirstOrDefault(site => site.ReservedByCurrentShuttle)?.Id;
            PlanetaryDetailsPanel.Visible = true;
        }

        if (PlanetaryDetailsPanel.Visible)
            UpdatePlanetaryPanel();
    }

    private void OnPlanetSelected(KoronusCelestialBodyState body)
    {
        if (_selectedPlanetaryBody == body.Id && PlanetaryDetailsPanel.Visible)
        {
            ClosePlanetaryDetails();
            return;
        }

        if (_selectedPlanetaryBody != body.Id)
            _selectedLandingSite = null;

        _selectedPlanetaryBody = body.Id;
        _planetaryPanelPosition = null;
        PlanetaryDetailsPanel.Visible = true;
        UpdatePlanetaryPanel();
    }

    private void UpdatePlanetaryPanel()
    {
        PlanetaryLandingSites.RemoveAllChildren();
        var body = _planetaryState.Bodies.FirstOrDefault(candidate => candidate.Id == _selectedPlanetaryBody);
        if (body == null)
        {
            PlanetaryDetailsPanel.Visible = false;
            _selectedLandingSite = null;
            return;
        }

        PlanetaryBodyName.Text = Ellipsize(body.Name, PlanetaryTitleCharacterLimit);
        PlanetaryBodyName.ToolTip = body.Name;
        var climate = Loc.GetString(string.IsNullOrEmpty(body.Climate)
            ? "koronus-planetary-climate-unknown"
            : body.Climate);
        var size = Loc.GetString(body.NavVisualRadius switch
        {
            < 180f => "koronus-planetary-size-small",
            < 320f => "koronus-planetary-size-medium",
            _ => "koronus-planetary-size-large",
        });
        var climateText = Loc.GetString("koronus-planetary-popup-climate", ("climate", climate));
        PlanetaryBodyClimate.Text = Ellipsize(climateText, PlanetaryInfoCharacterLimit);
        PlanetaryBodyClimate.ToolTip = climateText;
        PlanetaryBodySize.Text = Loc.GetString("koronus-planetary-popup-size", ("size", size));

        var isLandedHere = _planetaryState.LandedBody == body.Id;
        var selectedSite = body.LandingSites.FirstOrDefault(site => site.Id == _selectedLandingSite);
        if (_selectedLandingSite != null && selectedSite == null)
            _selectedLandingSite = null;

        var siteGroup = new ButtonGroup();

        foreach (var site in body.LandingSites)
        {
            var portAvailable = site.Enabled && !site.Occupied;
            var status = site.ReservedByCurrentShuttle
                ? Loc.GetString("koronus-planetary-nav-site-current")
                : !site.Enabled
                    ? Loc.GetString("koronus-planetary-nav-site-closed")
                    : site.Occupied
                        ? Loc.GetString("koronus-planetary-nav-site-occupied")
                        : Loc.GetString("koronus-planetary-nav-site-available");
            var button = new Button
            {
                Text = Ellipsize(site.Name, PlanetaryLandingSiteCharacterLimit),
                ToolTip = Loc.GetString(
                    "koronus-planetary-details-port-entry",
                    ("name", site.Name),
                    ("width", site.Size.X.ToString("0")),
                    ("height", site.Size.Y.ToString("0")),
                    ("status", status)),
                TextAlign = Label.AlignMode.Left,
                HorizontalExpand = true,
                MinHeight = 28f,
                ToggleMode = true,
                Group = siteGroup,
                StyleClasses = { "ButtonSquare" },
                Disabled = !portAvailable,
            };
            var selected = site.Id == _selectedLandingSite && (portAvailable || site.ReservedByCurrentShuttle);
            button.Pressed = selected;
            button.Label.ClipText = true;
            button.Label.FontColorOverride = selected
                ? Color.FromHex("#A6EF7C")
                : site.ReservedByCurrentShuttle
                ? Color.FromHex("#80CFFF")
                : portAvailable
                    ? Color.FromHex("#E0E0E0")
                    : Color.FromHex("#6F7774");
            button.OnPressed += _ => SelectLandingSite(body.Id, site.Id);
            PlanetaryLandingSites.AddChild(button);
        }

        PlanetaryLandingSiteScroll.Visible = true;
        PlanetaryLandingButton.Visible = !isLandedHere;
        PlanetaryLaunchButton.Visible = isLandedHere;
        PlanetaryLaunchButton.Disabled = !_planetaryState.CanLaunch || _planetaryState.Transit != null;
        PlanetaryLaunchButton.Label.FontColorOverride = PlanetaryLaunchButton.Disabled
            ? Color.FromHex("#686868")
            : Color.FromHex("#9BEF73");

        selectedSite = body.LandingSites.FirstOrDefault(site => site.Id == _selectedLandingSite);
        var canLandAtSelectedSite = selectedSite != null && CanLandAtSite(body, selectedSite);
        PlanetaryLandingButton.Disabled = !canLandAtSelectedSite;
        PlanetaryLandingButton.Label.FontColorOverride = canLandAtSelectedSite
            ? Color.FromHex("#9BEF73")
            : Color.FromHex("#686868");
    }

    protected override void FrameUpdate(FrameEventArgs args)
    {
        base.FrameUpdate(args);
        var progress = _ftlTime.ProgressAt(_timing.CurTime);
        BssFTLBar.Value = float.IsFinite(progress) ? progress : 1f;
        if (!_navigationSuppressed &&
            _timing.CurTime >= _nextBssScan &&
            _ftlState is not (FTLState.Starting or FTLState.Travelling or FTLState.Arriving))
        {
            BssRebuildButton.Disabled = false;
        }

        PositionPlanetaryPanel();
    }

    private void PositionPlanetaryPanel()
    {
        if (!PlanetaryDetailsPanel.Visible)
            return;

        // A landed shuttle deliberately hides NavRadar. Its arranged size then becomes zero, so
        // using it as the anchor leaves the manually positioned panel at the layout origin. The
        // surrounding navigation area remains arranged in both modes and is the stable anchor.
        var navigationSize = PlanetaryNavigationArea.Size;
        if (navigationSize.X <= 0f || navigationSize.Y <= 0f)
            return;

        var panelSize = PlanetaryDetailsPanel.Size;
        var panelWidth = panelSize.X > 0f ? panelSize.X : PlanetaryPopupWidth;
        var panelHeight = panelSize.Y > 0f ? panelSize.Y : PlanetaryPopupFallbackHeight;
        // PlanetaryDetailsPanel is a direct child of PlanetaryNavigationArea, therefore manual
        // positions are local to that container rather than to the outer shuttle window.
        var navigationOrigin = Vector2.Zero;
        var radarSize = NavRadar.Size;
        var radarOrigin = NavRadar.Position;
        Vector2 position;

        if (NavRadar.Visible &&
            _selectedPlanetaryBody != null &&
            NavRadar.TryGetPlanetVisual(_selectedPlanetaryBody, out var planetPosition, out var planetRadius))
        {
            var planetCentre = radarOrigin + planetPosition;
            var left = planetCentre.X - planetRadius - PlanetaryPopupGap - panelWidth;
            var right = planetCentre.X + planetRadius + PlanetaryPopupGap;
            var minimumX = radarOrigin.X + PlanetaryPopupEdgePadding;
            var maximumX = MathF.Max(minimumX, radarOrigin.X + radarSize.X - panelWidth - PlanetaryPopupEdgePadding);
            var fitsLeft = left >= minimumX;
            var fitsRight = right <= maximumX;
            var x = fitsLeft || !fitsRight ? left : right;
            var minimumY = radarOrigin.Y + PlanetaryPopupEdgePadding;
            var maximumY = MathF.Max(minimumY, radarOrigin.Y + radarSize.Y - panelHeight - PlanetaryPopupEdgePadding);
            position = new Vector2(
                Math.Clamp(x, minimumX, maximumX),
                Math.Clamp(planetCentre.Y - panelHeight * 0.5f, minimumY, maximumY));
        }
        else
        {
            position = navigationOrigin + new Vector2(
                MathF.Max((navigationSize.X - panelWidth) * 0.5f, PlanetaryPopupEdgePadding),
                MathF.Max((navigationSize.Y - panelHeight) * 0.5f, PlanetaryPopupEdgePadding));
        }

        position = new Vector2(
            MathF.Round(position.X * 2f) * 0.5f,
            MathF.Round(position.Y * 2f) * 0.5f);
        if (_planetaryPanelPosition != null &&
            Vector2.DistanceSquared(_planetaryPanelPosition.Value, position) < 0.01f)
        {
            return;
        }

        _planetaryPanelPosition = position;
        LayoutContainer.SetPosition(PlanetaryDetailsPanel, position);
    }

    private bool CanLandAtSite(KoronusCelestialBodyState body, KoronusLandingSiteState site)
    {
        return _planetaryState.CanLand &&
               body.InLandingApproachRange &&
               _planetaryState.Transit == null &&
               site.Enabled &&
               !site.Occupied;
    }

    private void SelectLandingSite(string bodyId, string siteId)
    {
        var body = _planetaryState.Bodies.FirstOrDefault(candidate => candidate.Id == bodyId);
        var site = body?.LandingSites.FirstOrDefault(candidate => candidate.Id == siteId);
        if (body == null || site == null || !site.Enabled || site.Occupied)
            return;

        _selectedLandingSite = siteId;
        UpdatePlanetaryPanel();
    }

    private void TryRequestPlanetaryLanding()
    {
        var body = _planetaryState.Bodies.FirstOrDefault(candidate => candidate.Id == _selectedPlanetaryBody);
        var site = body?.LandingSites.FirstOrDefault(candidate => candidate.Id == _selectedLandingSite);
        if (body == null || site == null || !CanLandAtSite(body, site))
            return;

        PlanetaryLandingRequest?.Invoke(body.Id, site.Id);
    }

    private void ClosePlanetaryDetails()
    {
        if (_planetaryState.LandedBody != null || _planetaryState.Transit != null)
            return;

        _selectedPlanetaryBody = null;
        _selectedLandingSite = null;
        _planetaryPanelPosition = null;
        PlanetaryDetailsPanel.Visible = false;
    }

    private static string Ellipsize(string text, int maximumCharacters)
    {
        if (text.Length <= maximumCharacters)
            return text;

        return text[..(maximumCharacters - 3)].TrimEnd() + "...";
    }

