    protected override void KeyBindUp(GUIBoundKeyEventArgs args)
    {
        if (FtlMode && ViewingMap != MapId.Nullspace)
        {
            if (args.Function == EngineKeyFunctions.UIClick)
            {
                var mapUid = _mapManager.GetMapEntityId(ViewingMap);

                var beaconsOnly = EntManager.TryGetComponent(mapUid, out FTLDestinationComponent? destComp) &&
                                  destComp.BeaconsOnly;

                var mapTransform = Matrix3Helpers.CreateInverseTransform(Offset, Angle.Zero);
                var pointerLocalPosition = args.RelativePixelPosition;

                // Beacon-only is an FTL restriction, not an autopilot restriction. Autopilot mode
                // must never turn a click on a beacon into an unexpected FTL jump.
                if (!AutopilotMode && beaconsOnly &&
                    TryGetBeacon(_beacons, mapTransform, pointerLocalPosition, PixelRect, out var foundBeacon, out _))
                {
                    RequestBeaconFTL?.Invoke(foundBeacon.Entity, _ftlAngle);
                }
                else if (AutopilotMode && TryGetHoveredGrid(_mapObjects, mapTransform, pointerLocalPosition,
                             out var targetGrid, out _))
                {
                    RequestAutopilotGrid?.Invoke(_entManager.GetNetEntity(targetGrid.Entity));
                }
                else
                {
                    // We'll send the "adjusted" position and server will adjust it back when relevant.
                    var mapCoords = new MapCoordinates(InverseMapPosition(pointerLocalPosition), ViewingMap);

                    RequestFTL?.Invoke(mapCoords, _ftlAngle);
                }
            }
        }

        base.KeyBindUp(args);
    }

    protected override void MouseWheel(GUIMouseWheelEventArgs args)
    {
        // Scroll handles FTL rotation if you're in FTL mode.
        if (FtlMode)
        {
            _ftlAngle -= Angle.FromDegrees(15f) * args.Delta.Y; // Mono Edit: Subtract instead of add to preserve clockwise rotation when scrolling up. (positive angles are actually counter-clockwise)
            _ftlAngle = _ftlAngle.Reduced();
            return;
        }

        base.MouseWheel(args);
    }

    private void DrawParallax(DrawingHandleScreen handle)
    {
        if (!EntManager.TryGetComponent(_shuttleEntity, out TransformComponent? shuttleXform) || shuttleXform.MapUid == null)
            return;

        // TODO: Figure out how the fuck to make this common between the 3 slightly different parallax methods and move to parallaxsystem.
        // Draw background texture
        var tex = _shuttles.GetTexture(shuttleXform.MapUid.Value);

        // Size of the texture in world units.
        var size = tex.Size * MinimapScale * 1f;

        var position = ScalePosition(new Vector2(-Offset.X, Offset.Y));
        var slowness = 1f;

        // The "home" position is the effective origin of this layer.
        // Parallax shifting is relative to the home, and shifts away from the home and towards the Eye centre.
        // The effects of this are such that a slowness of 1 anchors the layer to the centre of the screen, while a slowness of 0 anchors the layer to the world.
        // (For values 0.0 to 1.0 this is in effect a lerp, but it's deliberately unclamped.)
        // The ParallaxAnchor adapts the parallax for station positioning and possibly map-specific tweaks.
        var home = Vector2.Zero;
        var scrolled = Vector2.Zero;

        // Origin - start with the parallax shift itself.
        var originBL = (position - home) * slowness + scrolled;

        // Place at the home.
        originBL += home;

        // Centre the image.
        originBL -= size / 2;

        // Remove offset so we can floor.
        var botLeft = new Vector2(0f, 0f);
        var topRight = botLeft + PixelSize;

        var flooredBL = botLeft - originBL;

        // Floor to background size.
        flooredBL = (flooredBL / size).Floored() * size;

        // Re-offset.
        flooredBL += originBL;

        for (var x = flooredBL.X; x < topRight.X; x += size.X)
        {
            for (var y = flooredBL.Y; y < topRight.Y; y += size.Y)
            {
                handle.DrawTextureRect(tex, new UIBox2(x, y, x + size.X, y + size.Y));
            }
        }
    }

    /// <summary>
    /// Gets the map objects that intersect the viewport.
    /// </summary>
    /// <param name="mapObjects"></param>
    /// <returns></returns>
    private List<IMapObject> GetViewportMapObjects(Matrix3x2 matty, List<IMapObject> mapObjects)
    {
        var results = new List<IMapObject>();
        var enlargement = new Vector2i((int) (16 * UIScale), (int) (16 * UIScale));
        var viewBox = new UIBox2i(Vector2i.Zero - enlargement, PixelSize + enlargement);

        foreach (var mapObj in mapObjects)
        {
            // If it's a grid-map skip it.
            if (mapObj is GridMapObject gridObj && (EntManager.HasComponent<MapComponent>(gridObj.Entity) || !_entManager.EntityExists(gridObj.Entity))) // Frontier: add EntityExists
                continue;

            var mapCoords = _shuttles.GetMapCoordinates(mapObj);

            var relativePos = Vector2.Transform(mapCoords.Position, matty);
            relativePos = relativePos with { Y = -relativePos.Y };
            var uiPosition = ScalePosition(relativePos);

            if (!viewBox.Contains(uiPosition.Floored()))
                continue;

            results.Add(mapObj);
        }

        return results;
    }

    private bool TryGetHoveredGrid(
        IEnumerable<IMapObject> mapObjects,
        Matrix3x2 mapTransform,
        Vector2 mousePosition,
        out GridMapObject targetGrid,
        out Vector2 targetPosition)
    {
        targetGrid = default!;
        targetPosition = default;
        // mousePosition and uiPosition are screen pixels, so the capture radius must be too.
        // GetMapObjectRadius is in world units and becomes enormous when the system map is zoomed out.
        var captureRadius = MathF.Max(14f * UIScale, GetMapMarkerRadius() * 1.4f);
        var closestDistance = captureRadius * captureRadius;
        var foundTarget = false;

        foreach (var mapObject in mapObjects)
        {
            if (mapObject is not GridMapObject grid ||
                grid.Entity == _shuttleEntity ||
                EntManager.HasComponent<MapComponent>(grid.Entity) ||
                !_entManager.EntityExists(grid.Entity))
            {
                continue;
            }

            var mapCoordinates = _shuttles.GetMapCoordinates(grid);
            if (mapCoordinates.MapId != ViewingMap)
                continue;

            var relativePosition = Vector2.Transform(mapCoordinates.Position, mapTransform);
            relativePosition = relativePosition with { Y = -relativePosition.Y };
            var uiPosition = ScalePosition(relativePosition);
            var distance = Vector2.DistanceSquared(mousePosition, uiPosition);
            if (distance > closestDistance)
                continue;

            closestDistance = distance;
            targetGrid = grid;
            targetPosition = uiPosition;
            foundTarget = true;
        }

        return foundTarget;
    }

    private void DrawAutopilotGridCapture(DrawingHandleScreen handle, Vector2 position, float radius, string? targetName)
    {
        var color = Color.LimeGreen;
        var innerRadius = radius * 0.68f;
        handle.DrawCircle(position, innerRadius, color.WithAlpha(0.08f));
        handle.DrawCircle(position, innerRadius, color, filled: false);

        foreach (var direction in AutopilotCaptureDirections)
        {
            var normal = new Vector2(-direction.Y, direction.X);
            var tip = position + direction * (radius * 0.94f);
            var tail = position + direction * (radius * 1.54f);
            var wingBase = tip + direction * (radius * 0.24f);
            var wingWidth = radius * 0.28f;
            handle.DrawLine(tail, tip, color);
            handle.DrawLine(tip, wingBase + normal * wingWidth, color);
            handle.DrawLine(tip, wingBase - normal * wingWidth, color);
        }

        // A grid can temporarily have no replicated metadata name. Fluent arguments cannot be null.
        var displayName = string.IsNullOrWhiteSpace(targetName)
            ? Loc.GetString("shuttle-console-unknown")
            : targetName;
        var label = Loc.GetString("shuttle-console-autopilot-grid-lock", ("name", displayName));
        var labelSize = handle.GetDimensions(_font, label, 1f);
        handle.DrawString(
            _font,
            position + new Vector2(-labelSize.X / 2f, radius * 1.82f),
            label,
            color);
    }

    private static void DrawAutopilotPointMarker(DrawingHandleScreen handle, Vector2 position, float radius, Color color)
    {
        var crossRadius = radius * 0.42f;
        handle.DrawCircle(position, crossRadius, color.WithAlpha(0.06f));
        handle.DrawCircle(position, crossRadius, color, filled: false);
        handle.DrawLine(position - Vector2.UnitX * radius, position + Vector2.UnitX * radius, color);
        handle.DrawLine(position - Vector2.UnitY * radius, position + Vector2.UnitY * radius, color);
    }

    protected override void Draw(DrawingHandleScreen handle)
    {
        base.Draw(handle);

        if (ViewingMap == MapId.Nullspace)
            return;

        var mapObjects = _mapObjects;
        DrawRecenter();

        if (InFtl || mapObjects.Count == 0)
        {
            DrawBacking(handle);
            DrawNoSignal(handle);
            return;
        }

        DrawParallax(handle);

        var viewedMapUid = _mapManager.GetMapEntityId(ViewingMap);
        var matty = Matrix3Helpers.CreateInverseTransform(Offset, Angle.Zero);
        var realTime = _timing.RealTime;
        var viewBox = new Box2(Offset - WorldRangeVector, Offset + WorldRangeVector);
        var viewportObjects = GetViewportMapObjects(matty, mapObjects);
        _viewportExclusions.Clear();

        // Draw our FTL range + no FTL zones
        // Do it up here because we want this layered below most things.
        if ((FtlMode || ShowFTLRangeOnly) && ShowFTLRange) // Mono
        {
            if (EntManager.TryGetComponent<TransformComponent>(_shuttleEntity, out var shuttleXform))
            {
                var gridUid = _shuttleEntity.Value;
                var gridPhysics = _physicsQuery.GetComponent(gridUid);
                var (gridPos, gridRot) = _xformSystem.GetWorldPositionRotation(shuttleXform);
                gridPos = Maps.GetGridPosition((gridUid, gridPhysics), gridPos, gridRot);

                var gridRelativePos = Vector2.Transform(gridPos, matty);
                gridRelativePos = gridRelativePos with { Y = -gridRelativePos.Y };
                var gridUiPos = ScalePosition(gridRelativePos);

                var range = _shuttles.GetFTLRange(gridUid);
                range *= MinimapScale;
                handle.DrawCircle(gridUiPos, range, Color.Gold, filled: false);
            }
        }

        if (_shuttleEntity is { } shuttle &&
            EntManager.TryGetComponent<TransformComponent>(shuttle, out var shuttleTransform) &&
            EntManager.TryGetComponent<KoronusSystemBoundaryComponent>(viewedMapUid, out var sectorBoundary) &&
            Vector2.DistanceSquared(_xformSystem.GetWorldPosition(shuttleTransform), sectorBoundary.Origin) >=
            sectorBoundary.Radius * sectorBoundary.WarningFraction *
            (sectorBoundary.Radius * sectorBoundary.WarningFraction))
        {
            var boundaryPosition = Vector2.Transform(sectorBoundary.Origin, matty);
            var boundaryCenter = ScalePosition(boundaryPosition with { Y = -boundaryPosition.Y });
            var boundaryRadius = sectorBoundary.Radius * MinimapScale;

            handle.DrawCircle(boundaryCenter, boundaryRadius, Color.Red.WithAlpha(0.9f), filled: false);
        }

        var exclusionColor = Color.Red;

        // Exclusions need a bumped range so we check all the ones on the map.
        foreach (var mapObj in mapObjects)
        {
            if (mapObj is not ShuttleExclusionObject exclusion)
                continue;

            // Check if it even intersects the viewport.
            var coords = EntManager.GetCoordinates(exclusion.Coordinates);
            var mapCoords = _xformSystem.ToMapCoordinates(coords);
            var enlargedBounds = viewBox.Enlarged(exclusion.Range);

            if (mapCoords.MapId != ViewingMap ||
                !enlargedBounds.Contains(mapCoords.Position))
            {
                continue;
            }

            var adjustedPos = Vector2.Transform(mapCoords.Position, matty);
            var localPos = ScalePosition(adjustedPos with { Y = -adjustedPos.Y });
            handle.DrawCircle(localPos, exclusion.Range * MinimapScale, exclusionColor.WithAlpha(0.05f));
            handle.DrawCircle(localPos, exclusion.Range * MinimapScale, exclusionColor, filled: false);

            _viewportExclusions.Add(exclusion);
        }

        _verts.Clear();
        _edges.Clear();
        _strings.Clear();

        // Add beacons if relevant.
        var beaconsOnly = _shuttles.IsBeaconMap(viewedMapUid);
        var controlLocalBounds = PixelRect;
        _beacons.Clear();

        if (ShowBeacons)
        {
            var beaconColor = Color.AliceBlue;

            foreach (var (beaconName, coords, mapO) in GetBeacons(viewportObjects, matty, controlLocalBounds))
            {
                var localPos = Vector2.Transform(coords.Position, matty);
                localPos = localPos with { Y = -localPos.Y };
                var beaconUiPos = ScalePosition(localPos);

                // Get company color if the beacon has it
                var displayColor = beaconColor;
                if (mapO is GridMapObject gridObj &&
                    EntManager.TryGetComponent(gridObj.Entity, out Shared._Mono.Company.CompanyComponent? companyComp) &&
                    !string.IsNullOrEmpty(companyComp.CompanyName) &&
                    IoCManager.Resolve<IPrototypeManager>().TryIndex<CompanyPrototype>(companyComp.CompanyName, out var beaconCompanyProto))
                {
                    displayColor = Color.FromSrgb(beaconCompanyProto.Color);
                }

                var existingVerts = _verts.GetOrNew(displayColor);
                var existingEdges = _edges.GetOrNew(displayColor);

                AddMapMarker(existingEdges, existingVerts, beaconUiPos, GetMapMarkerRadius(0.75f), SystemMapMarker.Beacon, Angle.Zero);
                _beacons.Add(mapO);

                var existingStrings = _strings.GetOrNew(displayColor);
                existingStrings.Add((beaconUiPos, beaconName, true));
            }
        }

        foreach (var mapObj in viewportObjects)
        {
            if (mapObj is not GridMapObject gridObj || !EntManager.TryGetComponent(gridObj.Entity, out MapGridComponent? mapGrid))
                continue;

            Entity<MapGridComponent> grid = (gridObj.Entity, mapGrid);
            IFFComponent? iffComp = null;

            // Rudimentary IFF for now, if IFF hiding on then we don't show on the map at all
            if (grid.Owner != _shuttleEntity &&
                EntManager.TryGetComponent(grid, out iffComp) &&
                (iffComp.Flags & IFFFlags.Hide) != 0x0)
            {
                continue;
            }

            // Mono
            var hideLabel = iffComp != null && (iffComp.Flags & IFFFlags.HideLabel) != 0x0;
            var detectionLevel = _console == null ? DetectionLevel.Detected : _detection.IsGridDetected(grid.Owner, _console.Value);
            var detected = detectionLevel != DetectionLevel.Undetected || !hideLabel;
            var blipOnly = detectionLevel != DetectionLevel.Detected;
            if (!detected)
                continue;

            var hideColor = hideLabel && iffComp != null && (iffComp.Flags & IFFFlags.AlwaysShowColor) == 0x0;
            var gridColor = hideColor ? blipOnly ? Color.Orange : Color.White : _shuttles.GetIFFColor(grid, self: _shuttleEntity == grid.Owner, component: iffComp);

            var existingVerts = _verts.GetOrNew(gridColor);
            var existingEdges = _edges.GetOrNew(gridColor);

            var gridPhysics = _physicsQuery.GetComponent(grid.Owner);
            var (gridPos, gridRot) = _xformSystem.GetWorldPositionRotation(grid.Owner);
            gridPos = Maps.GetGridPosition((grid, gridPhysics), gridPos, gridRot);

            var gridRelativePos = Vector2.Transform(gridPos, matty);
            gridRelativePos = gridRelativePos with { Y = -gridRelativePos.Y };
            var gridUiPos = ScalePosition(gridRelativePos);

            AddMapMarker(
                existingEdges,
                existingVerts,
                gridUiPos,
                GetMapMarkerRadius(),
                GetGridMarkerKind(grid.Owner, iffComp),
                -gridRot);

            // Mono - now always has label
            // Text
            // if (iffComp != null && (iffComp.Flags & IFFFlags.HideLabel) != 0x0)
            //    continue;

            // Force drawing it at this point.
            // Mono
            var iffText = hideLabel ?
                detectionLevel == DetectionLevel.PartialDetected ?
                    Loc.GetString($"shuttle-console-signature-infrared")
                    : _detection.HandleUnknownMassLabel(grid.Owner)
                : _shuttles.GetIFFLabel(grid, self: true, component: iffComp);

            if (string.IsNullOrEmpty(iffText))
                continue;

            var existingStrings = _strings.GetOrNew(gridColor);
            existingStrings.Add((gridUiPos, iffText, !hideLabel));
        }

        // Batch the colors whoopie
        // really only affects forks with lots of grids.
        foreach (var (color, sendVerts) in _verts)
        {
            handle.DrawPrimitives(DrawPrimitiveTopology.TriangleList, sendVerts, color.WithAlpha(0.12f));
        }

        foreach (var (color, sendEdges) in _edges)
        {
            handle.DrawPrimitives(DrawPrimitiveTopology.LineList, sendEdges, color);
        }

        foreach (var (color, sendStrings) in _strings)
        {
            var adjustedColor = Color.FromSrgb(color);

            foreach (var (gridUiPos, iffText, hasLabel) in sendStrings)
            {
                var textWidth = handle.GetDimensions(_font, iffText, 1f);

                // Split text into lines
                var lines = iffText.Split('\n');
                var mainLabel = lines[0];
                var mainTextWidth = handle.GetDimensions(_font, mainLabel, 1f);

                // Get company color if entity has CompanyComponent
                var displayColor = adjustedColor;
                if (hasLabel)
                {
                    foreach (var mapObj in viewportObjects)
                    {
                        if (mapObj is GridMapObject gridObj &&
                            EntManager.TryGetComponent(gridObj.Entity, out Shared._Mono.Company.CompanyComponent? companyComp) &&
                            !string.IsNullOrEmpty(companyComp.CompanyName) &&
                            IoCManager.Resolve<IPrototypeManager>().TryIndex<CompanyPrototype>(companyComp.CompanyName, out var gridCompanyProto))
                        {
                            displayColor = Color.FromSrgb(gridCompanyProto.Color);
                            break;
                        }
                    }
                }

                // Draw main ship label with company color if available
                handle.DrawString(_font, gridUiPos + mainTextWidth with { X = -mainTextWidth.X / 2f, Y = mainTextWidth.Y * UIScale }, mainLabel, displayColor);

                // Draw company label if present
                if (hasLabel && lines.Length > 1)
                {
                    var companyLabel = lines[1];
                    var companyTextWidth = handle.GetDimensions(_font, companyLabel, 1f);
                    var companyLabelOffset = mainTextWidth with
                    {
                        X = -companyTextWidth.X / 2f,
                        Y = mainTextWidth.Y * 2 * UIScale
                    };

                    handle.DrawString(_font, gridUiPos + companyLabelOffset, companyLabel, displayColor);
                }
            }
        }

        var mousePos = _inputs.MouseScreenPosition;
        var mouseLocalPos = GetLocalPosition(mousePos);

        // Draw dotted line from our own shuttle entity to mouse.
        if (FtlMode && !ShowFTLRangeOnly)
        {
            if (mousePos.Window != WindowId.Invalid)
            {
                // If mouse inbounds then draw it.
                if (_shuttleEntity != null && controlLocalBounds.Contains(mouseLocalPos.Floored()) &&
                    EntManager.TryGetComponent(_shuttleEntity, out TransformComponent? shuttleXform) &&
                    shuttleXform.MapID != MapId.Nullspace)
                {
                    // If it's a beacon only map then snap the mouse to a nearby spot.
                    ShuttleBeaconObject foundBeacon = default;

                    // Check for beacons around mouse and snap to that.
                    if (beaconsOnly && TryGetBeacon(viewportObjects, matty, mouseLocalPos, controlLocalBounds, out foundBeacon, out var foundLocalPos))
                    {
                        mouseLocalPos = foundLocalPos;
                    }

                    GridMapObject? capturedGrid = null;
                    var capturedPosition = Vector2.Zero;
                    if (AutopilotMode && TryGetHoveredGrid(
                            viewportObjects,
                            matty,
                            mouseLocalPos,
                            out var gridCapture,
                            out capturedPosition))
                    {
                        mouseLocalPos = capturedPosition;
                        capturedGrid = gridCapture;
                    }

                    var grid = EntManager.GetComponent<MapGridComponent>(_shuttleEntity.Value);

                    var (gridPos, gridRot) = _xformSystem.GetWorldPositionRotation(shuttleXform);
                    gridPos = Maps.GetGridPosition(_shuttleEntity.Value, gridPos, gridRot);

                    // do NOT apply LocalCenter operation here because it will be adjusted in FTLFree.
                    var mouseMapPos = InverseMapPosition(mouseLocalPos);

                    var insideSectorBoundary = !EntManager.TryGetComponent<KoronusSystemBoundaryComponent>(viewedMapUid, out var targetBoundary) ||
                                               (mouseMapPos - targetBoundary.Origin).LengthSquared() <= targetBoundary.Radius * targetBoundary.Radius;

                    var ftlFree = insideSectorBoundary &&
                                  (((!beaconsOnly || foundBeacon != default) &&
                                    _shuttles.FTLFree(_shuttleEntity.Value, new EntityCoordinates(viewedMapUid, mouseMapPos), _ftlAngle, _viewportExclusions))
                                   || NoFTLRange); // Mono

                    var color = ftlFree ? Color.LimeGreen : Color.Magenta;

                    if (capturedGrid is { } selectedGrid)
                    {
                        DrawAutopilotGridCapture(handle, capturedPosition, GetMapMarkerRadius() * 1.65f, selectedGrid.Name);
                    }
                    else if (AutopilotMode)
                    {
                        DrawAutopilotPointMarker(handle, mouseLocalPos, GetMapMarkerRadius() * 0.72f, color);
                    }

                    var gridRelativePos = Vector2.Transform(gridPos, matty);
                    gridRelativePos = gridRelativePos with { Y = -gridRelativePos.Y };
                    var gridUiPos = ScalePosition(gridRelativePos);

                    // Draw FTL buffer around the mouse.
                    var ourFTLBuffer = _shuttles.GetFTLBufferRange(_shuttleEntity.Value, grid);
                    ourFTLBuffer *= MinimapScale;
                    handle.DrawCircle(mouseLocalPos, ourFTLBuffer, Color.Magenta.WithAlpha(0.01f));
                    handle.DrawCircle(mouseLocalPos, ourFTLBuffer, Color.Magenta, filled: false);

                    // Draw line from our shuttle to target
                    // Might need to clip the line if it's too far? But my brain wasn't working so F.
                    handle.DrawDottedLine(gridUiPos, mouseLocalPos, color, (float)realTime.TotalSeconds * 30f);

                    // Draw shuttle pre-vis
                    var mouseVerts = GetMapObject(mouseLocalPos, -_ftlAngle, scale: MinimapScale); // Mono Edit: UI controls and the map are in different coordinate orientations, so the angle has to be negated. (shuttle Y is UI control -Y)

                    handle.DrawPrimitives(DrawPrimitiveTopology.TriangleFan, mouseVerts.Span, color.WithAlpha(0.05f));
                    handle.DrawPrimitives(DrawPrimitiveTopology.LineLoop, mouseVerts.Span, color);

                    // Draw a notch indicating direction.
                    var ftlLength = GetMapObjectRadius() + 16f;
                    var ftlEnd = mouseLocalPos + (-_ftlAngle).RotateVec(new Vector2(0f, -ftlLength)); // Mono Edit: UI controls and the map are in different coordinate orientations, so the angle has to be negated. (shuttle Y is UI control -Y)

                    handle.DrawLine(mouseLocalPos, ftlEnd, color);
                }
            }
        }

        // Draw the coordinates
        var mapOffset = MidPointVector;

        if (mousePos.Window != WindowId.Invalid &&
            controlLocalBounds.Contains(mouseLocalPos.Floored()))
        {
            mapOffset = mouseLocalPos;
        }

        mapOffset = InverseMapPosition(mapOffset);
        var coordsText = $"{mapOffset.X:0.0}, {mapOffset.Y:0.0}";

        // Get company color if available
        var coordColor = Color.White;
        if (_shuttleEntity != null &&
            EntManager.TryGetComponent(_shuttleEntity.Value, out Shared._Mono.Company.CompanyComponent? shipCompanyComp) &&
            !string.IsNullOrEmpty(shipCompanyComp.CompanyName) &&
            IoCManager.Resolve<IPrototypeManager>().TryIndex<CompanyPrototype>(shipCompanyComp.CompanyName, out var shipCompanyProto))
        {
            coordColor = shipCompanyProto.Color;
        }

        DrawData(handle, coordsText, coordColor);
    }

    private SystemMapMarker GetGridMarkerKind(EntityUid grid, IFFComponent? iff)
    {
        if (grid == _shuttleEntity)
            return SystemMapMarker.OwnShuttle;

        if (EntManager.HasComponent<StationMemberComponent>(grid))
            return SystemMapMarker.Station;

        return iff != null && (iff.Flags & IFFFlags.IsPlayerShuttle) != 0x0
            ? SystemMapMarker.Shuttle
            : SystemMapMarker.Contact;
    }

    private static void AddMapMarker(
        List<Vector2> edges,
        List<Vector2> verts,
        Vector2 position,
        float radius,
        SystemMapMarker marker,
        Angle rotation)
    {
        switch (marker)
        {
            case SystemMapMarker.Beacon:
                AddBeaconMarker(edges, verts, position, radius, rotation);
                break;
            case SystemMapMarker.OwnShuttle:
                AddShuttleMarker(edges, verts, position, radius, rotation, true);
                break;
            case SystemMapMarker.Shuttle:
                AddShuttleMarker(edges, verts, position, radius, rotation, false);
                break;
            case SystemMapMarker.Station:
                AddStationMarker(edges, verts, position, radius, rotation);
                break;
            case SystemMapMarker.Contact:
                AddContactMarker(edges, verts, position, radius, rotation);
                break;
        }
    }

    private static void AddBeaconMarker(List<Vector2> edges, List<Vector2> verts, Vector2 position, float radius, Angle rotation)
    {
        AddRegularPolygon(edges, verts, position, radius * 0.58f, 6, rotation + Angle.FromDegrees(30f));

        for (var i = 0; i < 4; i++)
        {
            var direction = (rotation + Angle.FromDegrees(90f * i)).RotateVec(new Vector2(0f, -1f));
            AddLine(edges, position + direction * radius * 0.72f, position + direction * radius * 1.15f);
        }
    }

    private static void AddShuttleMarker(
        List<Vector2> edges,
        List<Vector2> verts,
        Vector2 position,
        float radius,
        Angle rotation,
        bool ownShuttle)
    {
        if (ownShuttle)
            AddCommandShuttleMarker(edges, verts, position, radius, rotation);
        else
            AddPlayerShuttleMarker(edges, verts, position, radius, rotation);
    }

    /// <summary>
