    {
        var margin = 5f;
        var font = _font;
        var dimensions = handle.GetDimensions(font, text, 1f);
        var position = new Vector2(margin, PixelHeight - dimensions.Y - margin);
        handle.DrawString(font, position, text, color);
    }
