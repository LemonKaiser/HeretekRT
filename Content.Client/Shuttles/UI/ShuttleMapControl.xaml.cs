    private static void AddEngineNozzle(
        List<Vector2> edges,
        Vector2 position,
        float radius,
        Angle rotation,
        float x)
    {
        var engine = MarkerPoint(position, radius, rotation, x, 0.84f);
        var exhaust = MarkerPoint(position, radius, rotation, x, 1.5f);
        var outer = MarkerPoint(position, radius, rotation, x * 1.2f, 1.22f);
        var inner = MarkerPoint(position, radius, rotation, x * 0.79f, 1.22f);

        AddLine(edges, engine, exhaust);
        AddLine(edges, outer, exhaust);
        AddLine(edges, exhaust, inner);
    }

    private static void AddStationMarker(List<Vector2> edges, List<Vector2> verts, Vector2 position, float radius, Angle rotation)
    {
        var ringRotation = rotation + Angle.FromDegrees(22.5f);
        AddRegularPolygon(edges, verts, position, radius * 1.16f, 8, ringRotation, false);
        AddRegularPolygon(edges, verts, position, radius * 0.66f, 8, ringRotation, false);
        AddRegularPolygon(edges, verts, position, radius * 0.34f, 6, rotation + Angle.FromDegrees(30f));

        for (var i = 0; i < 4; i++)
        {
            var direction = (rotation + Angle.FromDegrees(90f * i)).RotateVec(new Vector2(0f, -1f));
            var normal = new Vector2(-direction.Y, direction.X);
            var armStart = position + direction * radius * 0.36f;
            var armEnd = position + direction * radius * 1.36f;
            var dockWidth = radius * 0.17f;

            AddLine(edges, armStart, armEnd);
            AddLine(edges, armEnd - normal * dockWidth, armEnd + normal * dockWidth);
            AddLine(edges, armEnd - normal * dockWidth, armEnd - direction * radius * 0.16f);
            AddLine(edges, armEnd + normal * dockWidth, armEnd - direction * radius * 0.16f);
        }
    }

    private static void AddContactMarker(List<Vector2> edges, List<Vector2> verts, Vector2 position, float radius, Angle rotation)
    {
        AddRegularPolygon(edges, verts, position, radius * 0.8f, 6, rotation + Angle.FromDegrees(30f));
        AddLine(edges, MarkerPoint(position, radius, rotation, -0.52f, 0f), MarkerPoint(position, radius, rotation, 0.52f, 0f));
        AddLine(edges, MarkerPoint(position, radius, rotation, 0f, -0.52f), MarkerPoint(position, radius, rotation, 0f, 0.52f));
    }

    private static Vector2 MarkerPoint(Vector2 position, float radius, Angle rotation, float x, float y)
    {
        return position + rotation.RotateVec(new Vector2(x, y) * radius);
    }

    private static void AddRegularPolygon(
        List<Vector2> edges,
        List<Vector2> verts,
        Vector2 position,
        float radius,
        int sides,
        Angle rotation,
        bool filled = true)
    {
        var first = MarkerPoint(position, radius, rotation, 0f, -1f);
        var previous = first;

        for (var i = 1; i <= sides; i++)
        {
            var current = MarkerPoint(position, radius, rotation + new Angle(Math.Tau * i / sides), 0f, -1f);
            if (filled)
                AddTriangle(verts, position, previous, current);

            AddLine(edges, previous, current);
            previous = current;
        }
    }

    private static void AddTriangle(List<Vector2> verts, Vector2 first, Vector2 second, Vector2 third)
    {
        verts.Add(first);
        verts.Add(second);
        verts.Add(third);
    }

    private static void AddLine(List<Vector2> edges, Vector2 start, Vector2 end)
    {
        edges.Add(start);
        edges.Add(end);
    }

    /// <summary>
    /// Returns the beacons that intersect the viewport.
    /// </summary>
    private IEnumerable<(string Beacon, MapCoordinates Coordinates, IMapObject MapObject)> GetBeacons(List<IMapObject> mapObjs, Matrix3x2 mapTransform, UIBox2i area)
    {
        foreach (var mapO in mapObjs)
        {
            if (mapO is not ShuttleBeaconObject beacon)
                continue;

            var beaconCoords = _xformSystem.ToMapCoordinates(EntManager.GetCoordinates(beacon.Coordinates));
            var position = Vector2.Transform(beaconCoords.Position, mapTransform);
            var localPos = ScalePosition(position with {Y = -position.Y});

            // If beacon not on screen then ignore it.
            if (!area.Contains(localPos.Floored()))
                continue;

            yield return (beacon.Name, beaconCoords, mapO);
        }
    }

    private float GetMapObjectRadius(float scale = 1f) => WorldRange / 40f * scale;

    private float GetMapMarkerRadius(float scale = 1f) => GetMapObjectRadius(scale) * MinimapScale;

    private ValueList<Vector2> GetMapObject(Vector2 localPos, Angle angle, float scale = 1f, bool scalePosition = false)
    {
        // Constant size diamonds
        var diamondRadius = GetMapObjectRadius();

        var mapObj = new ValueList<Vector2>(4)
        {
            localPos + angle.RotateVec(new Vector2(0f, -2f * diamondRadius)) * scale,
            localPos + angle.RotateVec(new Vector2(diamondRadius, 0f)) * scale,
            localPos + angle.RotateVec(new Vector2(0f, 2f * diamondRadius)) * scale,
            localPos + angle.RotateVec(new Vector2(-diamondRadius, 0f)) * scale,
        };

        if (scalePosition)
        {
            for (var i = 0; i < mapObj.Count; i++)
            {
                mapObj[i] = ScalePosition(mapObj[i]);
            }
        }

        return mapObj;
    }

    private bool TryGetBeacon(IEnumerable<IMapObject> mapObjects, Matrix3x2 mapTransform, Vector2 mousePos, UIBox2i area, out ShuttleBeaconObject foundBeacon, out Vector2 foundLocalPos)
    {
        // In pixels
        const float BeaconSnapRange = 32f;
        float nearestValue = float.MaxValue;
        foundLocalPos = Vector2.Zero;
        foundBeacon = default;

        foreach (var mapObj in mapObjects)
        {
            if (mapObj is not ShuttleBeaconObject beaconObj)
                continue;

            var beaconCoords = _xformSystem.ToMapCoordinates(EntManager.GetCoordinates(beaconObj.Coordinates));

            if (beaconCoords.MapId != ViewingMap)
                continue;

            // Invalid beacon?
            if (!_shuttles.CanFTLBeacon(beaconObj.Coordinates))
                continue;

            var position = Vector2.Transform(beaconCoords.Position, mapTransform);
            var localPos = ScalePosition(position with {Y = -position.Y});

            // If beacon not on screen then ignore it.
            if (!area.Contains(localPos.Floored()))
                continue;

            var distance = (localPos - mousePos).Length();

            if (distance > BeaconSnapRange * UIScale ||
                distance > nearestValue)
            {
                continue;
            }

            foundLocalPos = localPos;
            nearestValue = distance;
            foundBeacon = beaconObj;
        }

        return foundBeacon != default;
    }

