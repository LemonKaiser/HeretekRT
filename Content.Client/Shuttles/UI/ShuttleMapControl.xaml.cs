        var portTail = MarkerPoint(position, radius, rotation, -0.36f, 1.28f);
        var portEngine = MarkerPoint(position, radius, rotation, -0.88f, 0.88f);
        var portWing = MarkerPoint(position, radius, rotation, -1.22f, 0.22f);
        var portShoulder = MarkerPoint(position, radius, rotation, -0.48f, -0.52f);
        var cockpitTip = MarkerPoint(position, radius, rotation, 0f, -1.14f);
        var cockpitStarboard = MarkerPoint(position, radius, rotation, 0.25f, -0.36f);
        var cockpitPort = MarkerPoint(position, radius, rotation, -0.25f, -0.36f);
