        return body.BodyType switch
        {
            "Moon" => Color.FromHex("#A9B0B6"),
            "Asteroid" => Color.FromHex("#8F856C"),
            "Station" => Color.FromHex("#76C2D0"),
            _ => Color.FromHex("#8CC971"),
        };
    }

