    /// <summary>
    /// Returns the current UI-space centre and visible radius of a planet drawn by the NAV radar.
    /// The containing screen uses this to keep its compact information card attached to the body
    /// while the radar is panned, zoomed or the body advances along its orbit.
    /// </summary>
    public bool TryGetPlanetVisual(string bodyId, out Vector2 position, out float radius)
    {
        foreach (var target in _planetHitTargets)
        {
            if (target.Body.Id != bodyId)
                continue;

            position = target.Position / UIScale;
            radius = target.VisualRadius / UIScale;
            return true;
        }

        position = default;
        radius = 0f;
        return false;
    }

    public bool TryGetTargetGrid(EntityCoordinates coordinates, out EntityUid targetGrid)
    {
        targetGrid = default;
        var mapCoordinates = _transform.ToMapCoordinates(coordinates);
        if (mapCoordinates.MapId == MapId.Nullspace)
            return false;

        var bodyQuery = EntManager.GetEntityQuery<PhysicsComponent>();
        var captureRange = MathF.Max(1f, 18f * UIScale / MinimapScale);
        var closestDistance = captureRange * captureRange;

        foreach (var grid in _visibleGridsSet)
        {
            if (!bodyQuery.TryGetComponent(grid, out var body))
                continue;

            var gridCoordinates = _transform.ToMapCoordinates(new EntityCoordinates(grid, body.LocalCenter));
            if (gridCoordinates.MapId != mapCoordinates.MapId)
                continue;

            var distance = Vector2.DistanceSquared(gridCoordinates.Position, mapCoordinates.Position);
            if (distance > closestDistance)
                continue;

            closestDistance = distance;
            targetGrid = grid;
        }

        return targetGrid.IsValid();
    }

    public bool TryGetTargetBeacon(EntityCoordinates coordinates, out ShuttleBeaconObject targetBeacon)
    {
        targetBeacon = default;
        var mapCoordinates = _transform.ToMapCoordinates(coordinates);
        if (mapCoordinates.MapId == MapId.Nullspace)
            return false;

        var captureRange = MathF.Max(1f, 22f * UIScale / MinimapScale);
        var closestDistance = captureRange * captureRange;
        var found = false;
        foreach (var beacon in _bssBeacons)
        {
            var beaconCoordinates = _clientShuttles.GetMapCoordinates(beacon);
            if (beaconCoordinates.MapId != mapCoordinates.MapId)
                continue;

            var distance = Vector2.DistanceSquared(beaconCoordinates.Position, mapCoordinates.Position);
            if (distance > closestDistance)
                continue;

            closestDistance = distance;
            targetBeacon = beacon;
            found = true;
        }

        return found;
    }

    protected override void Draw(DrawingHandleScreen handle)
    {
        base.Draw(handle);

        DrawBacking(handle);
        DrawCircles(handle);

        // No data
        if (_coordinates == null || _rotation == null)
        {
            return;
        }

        var xformQuery = EntManager.GetEntityQuery<TransformComponent>();
        var fixturesQuery = EntManager.GetEntityQuery<FixturesComponent>();
        var bodyQuery = EntManager.GetEntityQuery<PhysicsComponent>();

        if (!xformQuery.TryGetComponent(_coordinates.Value.EntityId, out var xform)
            || xform.MapID == MapId.Nullspace)
        {
            return;
        }

        // Mono
        Draggable = ForcePannable;
        if (EntManager.TryGetComponent<RadarConsoleComponent>(_consoleEntity, out var radar))
            Draggable |= radar.Pannable;
        // Mono - TODO: remove _rotation and make it a method instead
        var coordEnt = _coordinates.Value.EntityId;
        var coordEntRot = _transform.GetWorldRotation(coordEnt);
        if (_angleFollow && EntManager.TryGetComponent<TransformComponent>(coordEnt, out var coordXform))
            _rotation = coordEntRot;
        else
            _rotation = Angle.Zero;

        var worldRot = _rotation.Value;
        var observerPosition = _transform.ToMapCoordinates(_coordinates.Value).Position;
        var mapPos = _transform.ToMapCoordinates(_coordinates.Value).Offset(_rotation.Value.RotateVec(Offset));
        var mapCoord = _transform.ToCoordinates(mapPos);
        var worldToShuttle = Matrix3Helpers.CreateTranslation(-mapCoord.Position) * Matrix3Helpers.CreateRotation(-worldRot);
        Matrix3x2.Invert(worldToShuttle, out var shuttleToWorld);
        var shuttleToView = Matrix3x2.CreateScale(new Vector2(MinimapScale, -MinimapScale)) * Matrix3x2.CreateTranslation(MidPointVector);
        var worldToView = worldToShuttle * shuttleToView;

        // Draw shields
        DrawShields(handle, xform, worldToShuttle);

        // Frontier Corvax: north line drawing
        DrawNorthLine(handle, worldRot);

        // Mono
        switch (_azimuthMode)
        {
            case RadarAzimuthMode.Edge:
                DrawAzimuthScale(handle, worldRot);
                break;
            case RadarAzimuthMode.Ring:
                DrawAzimuthRing(handle, worldRot);
                break;
        }

        if (worldRot != Angle.Zero)
        {
            DrawCompassOverlay(handle, coordEntRot);
        }

        // This is deliberately part of the ordinary radar pass: planet positions use the same
        // world-to-view matrix as grids, IFF contacts and docking ports.
        DrawCelestialNavigation(handle, worldToView);

        // Draw our grid in detail
        var ourGridId = xform.GridUid ?? _shuttleEntity;
        if (EntManager.TryGetComponent<MapGridComponent>(ourGridId, out var ourGrid) &&
            fixturesQuery.HasComponent(ourGridId.Value))
        {
            var ourGridToWorld = _transform.GetWorldMatrix(ourGridId.Value);
            var ourGridToShuttle = Matrix3x2.Multiply(ourGridToWorld, worldToShuttle);
            var ourGridToView = ourGridToShuttle * shuttleToView;
            var color = _shuttles.GetIFFColor(ourGridId.Value, self: true);

            DrawGrid(handle, ourGridToView, (ourGridId.Value, ourGrid), color);
            DrawDocks(handle, ourGridId.Value, ourGridToView);
        }

        // Draw radar position on the station without a per-frame vertex allocation.
        for (var i = 0; i < _radarPositionVertices.Length; i++)
        {
            _radarPositionVertices[i] = ScalePosition(RadarPosVertsCache[i]);
        }

        handle.DrawPrimitives(DrawPrimitiveTopology.TriangleFan, _radarPositionVertices, Color.Lime);

        var viewBounds = new Box2Rotated(new Box2(-WorldRange, -WorldRange, WorldRange, WorldRange).Translated(mapPos.Position), worldRot, mapPos.Position);
        var viewAABB = viewBounds.CalcBoundingBox();

        _grids.Clear();
        _mapManager.FindGridsIntersecting(xform.MapID, new Box2(mapPos.Position - MaxRadarRangeVector, mapPos.Position + MaxRadarRangeVector), ref _grids, approx: true, includeMap: false);

        // Mono edited: Frontier - collect blip location data outside foreach - more changes ahead
        _tempBlipDataList.Clear();

        _visibleGridsSet.Clear();

        // Draw other grids... differently
        foreach (var grid in _grids)
        {
            var gUid = grid.Owner;
            if (gUid == ourGridId || !fixturesQuery.HasComponent(gUid))
                continue;

            var gridBody = bodyQuery.GetComponent(gUid);
            EntManager.TryGetComponent<IFFComponent>(gUid, out var iff);

            if (!_shuttles.CanDraw(gUid, gridBody, iff))
                continue;

            var hideLabel = iff != null && (iff.Flags & IFFFlags.HideLabel) != 0x0;
            var noLabel = iff != null && (iff.Flags & IFFFlags.HideLabelAlways) != 0x0;
            var detectionLevel = _consoleEntity == null ? DetectionLevel.Detected : GetGridDetected(grid.Owner);
            var detected = detectionLevel != DetectionLevel.Undetected || !hideLabel;
            var blipOnly = detectionLevel != DetectionLevel.Detected; // don't show outline outside of detection radius even if IFF on
            if (!detected)
                continue;

            if (!blipOnly)
                _visibleGridsSet.Add(grid);
            var curGridToWorld = _transform.GetWorldMatrix(gUid);
            var curGridToView = curGridToWorld * worldToView;

            var hideColor = hideLabel && iff != null && (iff.Flags & IFFFlags.AlwaysShowColor) == 0x0;
            var labelColor = hideColor ? blipOnly ? Color.Orange : Color.White : _shuttles.GetIFFColor(grid, self: false, iff);
            var coordColor = new Color(labelColor.R * 0.8f, labelColor.G * 0.8f, labelColor.B * 0.8f, 0.5f);

            var isPlayerShuttle = iff != null && (iff.Flags & IFFFlags.IsPlayerShuttle) != 0x0;
            // Others default:
            // Color.FromHex("#FFC000FF")
            // Hostile default: Color.Firebrick
            // Mono
            var labelName = noLabel ? null : hideLabel ?
                detectionLevel == DetectionLevel.PartialDetected ?
                    Loc.GetString($"shuttle-console-signature-infrared")
                    : _detection.HandleUnknownMassLabel(grid.Owner)
                : _shuttles.GetIFFLabel(grid, self: false, component: iff);

            var shouldDrawIFF = ShowIFF && labelName != null;
            if (shouldDrawIFF)
            {
                if (IFFFilter != null)
                    shouldDrawIFF &= IFFFilter(gUid, grid.Comp, iff, hideLabel, labelName!);
                if (isPlayerShuttle)
                    shouldDrawIFF &= ShowIFFShuttles;
            }

            //var mapCenter = curGridToWorld. * gridBody.LocalCenter;
            //shouldDrawIFF = NfCheckShouldDrawIffRangeCondition(shouldDrawIFF, mapCenter, curGridToWorld); // Frontier code
            // Frontier: range checks // Mono
            var gridMapPos = _transform.ToMapCoordinates(new EntityCoordinates(gUid, gridBody.LocalCenter)).Position;
            if (!hideLabel) // Mono - show thermal signatures even at long range
                shouldDrawIFF = NfCheckShouldDrawIffRangeCondition(shouldDrawIFF, mapPos.Position - gridMapPos);
            // End Frontier

            // Mono
            var gridUiPosition = Vector2.Transform(gridBody.LocalCenter, curGridToView) / UIScale;

            if (shouldDrawIFF)
            {
                //var gridCentre = Vector2.Transform(gridBody.LocalCenter, curGridToView);
                //gridCentre.Y = -gridCentre.Y;

                // Frontier: IFF drawing functions
                // The actual position in the UI. We offset the matrix position to render it off by half its width
                // plus by the offset.
                //var uiPosition = ScalePosition(gridCentre) / UIScale;
                var uiPosition = gridUiPosition; // Mono

                // Confines the UI position within the viewport.
                var uiXCentre = (int)Width / 2;
                var uiYCentre = (int)Height / 2;
                var uiXOffset = uiPosition.X - uiXCentre;
                var uiYOffset = uiPosition.Y - uiYCentre;
                var uiDistance = MathF.Sqrt(uiXOffset * uiXOffset + uiYOffset * uiYOffset);
                if (uiDistance <= float.Epsilon)
                    uiDistance = 1f;

                var uiX = uiXCentre * uiXOffset / uiDistance;
                var uiY = uiYCentre * uiYOffset / uiDistance;

                var isOutsideRadarCircle = uiDistance > Math.Abs(uiX) && uiDistance > Math.Abs(uiY);
                if (isOutsideRadarCircle)
                {
                    // 0.95f for offsetting the icons slightly away from edge of radar so it doesnt clip.
                    uiX = uiXCentre * uiXOffset / uiDistance * 0.95f;
                    uiY = uiYCentre * uiYOffset / uiDistance * 0.95f;
                    uiPosition = new Vector2(
                        x: uiX + uiXCentre,
                        y: uiY + uiYCentre
                    );
                }

                var scaledMousePosition = GetMouseCoordinatesFromCenter().Position * UIScale;
                var isMouseOver = Vector2.Distance(scaledMousePosition, uiPosition * UIScale) < 30f;

                // Distant stations that are not player controlled ships
                var isDistantPOI = iff != null || (iff == null || (iff.Flags & IFFFlags.IsPlayerShuttle) == 0x0);

                var distance = Vector2.Distance(gridMapPos, mapPos.Position);

                if (!isOutsideRadarCircle || isDistantPOI || isMouseOver)
                {
                    // Shows decimal when distance is < 50m, otherwise pointless to show it.
                    var displayedDistance = distance < 50f ? $"{distance:0.0}" : distance < 1000 ? $"{distance:0}" : $"{distance / 1000:0.0}k";
                    var labelText = Loc.GetString("shuttle-console-iff-label", ("name", labelName)!, ("distance", displayedDistance));

                    var coordsText = $"({gridMapPos.X:0.0}, {gridMapPos.Y:0.0})";

                    #region Mono

                    // Why are the magic numbers 0.9 and 0.7 used? I have no fucking clue.
                    var labelDimensions = handle.GetDimensions(Font, labelText, 0.9f);
                    var blipSize = RadarBlipSize * 0.7f;

                    // The center of the radar in UI space.
                    var uiCenter = new Vector2(Width * 0.5f, Height * 0.5f);

                    // Whether the blip is on the left side of the center of the radar.
                    var isOnLeftSide = (uiPosition - uiCenter).X < 0;

                    // The UI position of the bottom-left corner of the label, relative to the UI center of the radar, when the label is right-aligned.
                    var labelPosition = uiPosition + new Vector2(-labelDimensions.X - blipSize, -labelDimensions.Y * 0.5f) - uiCenter;

                    // The radius and squared radius of the radar, in virtual pixels.
                    var radius = Width * 0.5f;
                    var squaredRadius = radius * radius;

                    // If true, flip the entire label to the right side of the blip and left-align it.
                    // We default to the label being on the left side of the blip because it looked better to me in testing. (arbitrary)
                    var labelRight = labelPosition + new Vector2(labelDimensions.X, 0f);
                    var labelBottom = labelPosition + new Vector2(0f, labelDimensions.Y);
                    var labelBottomRight = labelPosition + labelDimensions;
                    var flipLabel = isOnLeftSide &&
                                    (labelPosition.LengthSquared() > squaredRadius ||
                                     labelRight.LengthSquared() > squaredRadius ||
                                     labelBottom.LengthSquared() > squaredRadius ||
                                     labelBottomRight.LengthSquared() > squaredRadius);

                    // Calculate unscaled offsets.
                    var labelOffset = new Vector2()
                    {
                        X = flipLabel
                            ? blipSize // Label on the right side of the blip, left-aligned text.
                            : -labelDimensions.X - blipSize, // Label on the left side of the blip, right-aligned text.
                        Y = -labelDimensions.Y * 0.5f
                    };

                    #endregion Mono

                    // Get company color if entity has CompanyComponent
                    var displayColor = labelColor;
                    if (!hideLabel && EntManager.TryGetComponent(gUid, out Shared._Mono.Company.CompanyComponent? companyComp) &&
                        !string.IsNullOrEmpty(companyComp.CompanyName))
                    {
                        CompanyPrototype? prototype = null;
                        if (_prototypeManager.TryIndex(companyComp.CompanyName, out prototype) && prototype != null)
                        {
                            displayColor = prototype.Color;
                        }
                    }

                    var lineBreak = labelText.IndexOf('\n');
                    var mainLabel = lineBreak < 0 ? labelText : labelText[..lineBreak];
                    var companyLabel = !hideLabel && lineBreak >= 0
                        ? labelText[(lineBreak + 1)..]
                        : null;

                    // Draw main ship label with company color if available
                    handle.DrawString(Font, (uiPosition + labelOffset) * UIScale, mainLabel, UIScale * 0.9f, displayColor);

                    // Draw company label if present
                    if (companyLabel != null)
                    {
                        var companyLabelOffset = new Vector2(
                            labelOffset.X,
                            labelOffset.Y + handle.GetDimensions(Font, mainLabel, 0.9f).Y
                        );

                        handle.DrawString(Font, (uiPosition + companyLabelOffset) * UIScale, companyLabel, UIScale * 0.9f, displayColor);
                    }

                    if (isMouseOver && !HideCoords)
                    {
                        var coordDimensions = handle.GetDimensions(Font, coordsText, 0.7f);
                        var coordOffset = new Vector2()
                        {
                            X = uiPosition.X > Width / 2f
                                ? -coordDimensions.X - blipSize / 0.7f // right align the text to left of the blip (0.7 needed for scale)
                                : blipSize, // left align the text to the right of the blip
                            Y = labelOffset.Y + handle.GetDimensions(Font, mainLabel, 1f).Y + (companyLabel != null ? handle.GetDimensions(Font, companyLabel, 1f).Y : 0) + 5
                        };
                        handle.DrawString(Font, (uiPosition + coordOffset) * UIScale, coordsText, 0.7f * UIScale, displayColor);
                    }
                }

                NfAddBlipToList(_tempBlipDataList, isOutsideRadarCircle, uiPosition, uiXCentre, uiYCentre, labelColor, hideLabel ? default : gUid); // Frontier code
                // End Frontier: IFF drawing functions
            }

            // Detailed view
            var gridAABB = curGridToWorld.TransformBox(grid.Comp.LocalAABB);

            // Skip drawing if it's out of range.
            if (!gridAABB.Intersects(viewAABB))
                continue;

            // Mono
            if (!blipOnly)
            {
                DrawGrid(handle, curGridToView, grid, labelColor);
                DrawDocks(handle, gUid, curGridToView);
            }
        }

        // Draw the accumulated blips once. Drawing this inside the grid loop made the same growing
        // list render repeatedly, which was quadratic in the number of visible grids.
        NfDrawBlips(handle, _tempBlipDataList);

        // If we've set the controlling console, and it's on a different grid
        // to the shuttle itself, then draw an additional marker to help the
        // player determine where they are relative to the shuttle.
        if (_consoleEntity != null && xformQuery.TryGetComponent(_consoleEntity, out var consoleXform))
        {
            if (consoleXform.ParentUid != _coordinates.Value.EntityId)
            {
                var consolePositionWorld = _transform.GetWorldPosition((EntityUid)_consoleEntity);
                var p = Vector2.Transform(consolePositionWorld, worldToView);
                handle.DrawCircle(p, 5, Color.ToSrgb(Color.Cyan), true);
            }
        }

        #region Mono
        // Draw radar line
        // First, figure out which angle to draw.
        var updateRatio = _updateAccumulator / RadarUpdateInterval;

        Angle angle = updateRatio * Math.Tau;
        var origin = ScalePosition(-new Vector2(Offset.X, -Offset.Y));
        handle.DrawLine(origin, origin + angle.ToVec() * ScaledMinimapRadius * 1.42f, Color.Red.WithAlpha(0.1f));

        // Get blips
        var rawBlips = _blips.GetCurrentBlips();

        // Prepare view bounds for culling
        var monoViewBounds = new Box2(-3f, -3f, PixelSize.X + 3f, PixelSize.Y + 3f);

        // Draw blips using the same grid-relative transformation approach as docks
        foreach (var blip in rawBlips)
        {
            var position = Vector2.Transform(_transform.ToMapCoordinates(blip.Position).Position, worldToView);
            var color = blip.Config.Color.WithAlpha(0.8f);
            var box = new Box2Rotated(blip.Config.Bounds, 0);
            if (blip.Config.RespectZoom)
                box.Box = new Box2(box.Box.BottomLeft * MinimapScale, box.Box.TopRight * MinimapScale);
            if (blip.Config.Rotate)
                box.Rotation = worldRot - blip.Rotation;

            if (blip.GridUid is { } grid)
            {
                // check detection if we're on a grid and that grid isn't our grid
                if (!_visibleGridsSet.Contains(grid) && grid != ourGridId)
                    continue;
            }

            // Check if this blip is within view bounds before drawing
            if (monoViewBounds.Contains(position))
            {
                DrawBlipShape(handle, position, box, color, blip.Config.Shape);
            }
        }

        // Draw missile lines from the radar blips system
        var missileLines = _blips.GetMissileLines();
        foreach (var line in missileLines)
        {
            var startPos = new Vector2(line.PositionStart.X, line.PositionStart.Y);
            var startEnd = new Vector2(line.PositionEnd.X, line.PositionEnd.Y);
            var startPosInView = Vector2.Transform(startPos, worldToView);
            var endPosInView = Vector2.Transform(startEnd, worldToView);

            // Only draw lines if at least one endpoint is within view
            if (monoViewBounds.Contains(startPosInView) || monoViewBounds.Contains(endPosInView))
            {
                // Draw the line with the specified thickness and color
                handle.DrawLine(startPosInView, endPosInView, line.Color);
            }
        }

        // Draw hitscan lines from the radar blips system
        var hitscanLines = _blips.GetHitscanLines();
        foreach (var line in hitscanLines)
        {
            var startPosInView = Vector2.Transform(line.Start, worldToView);
            var endPosInView = Vector2.Transform(line.End, worldToView);

            // Only draw lines if at least one endpoint is within view
            if (monoViewBounds.Contains(startPosInView) || monoViewBounds.Contains(endPosInView))
            {
                // Draw the line with the specified thickness and color
                handle.DrawLine(startPosInView, endPosInView, line.Color);

                // For thicker lines, draw multiple lines side by side
                if (line.Thickness > 1.0f)
                {
                    // Calculate perpendicular vector for thickness
                    var dir = (endPosInView - startPosInView).Normalized();
                    var perpendicular = new Vector2(-dir.Y, dir.X) * 0.5f;

                    // Draw additional lines for thickness
                    for (float i = 1; i <= line.Thickness; i += 1.0f)
                    {
                        var offset = perpendicular * i;
                        handle.DrawLine(startPosInView + offset, endPosInView + offset, line.Color);
                        handle.DrawLine(startPosInView - offset, endPosInView - offset, line.Color);
                    }
                }
            }
        }

        DrawBssNavigation(handle, xform.MapID, ourGridId, worldToView);
        DrawKoronusBoundary(handle, xform.MapID, observerPosition, worldToView);
        #endregion
    }

    private void DrawBssNavigation(
        DrawingHandleScreen handle,
        MapId mapId,
        EntityUid? ourGridId,
        Matrix3x2 worldToView)
    {
        if (TargetingMode == ShuttleNavTargetingMode.None || ourGridId == null || !_isMouseInside)
            return;

        var targetCoordinates = GetMouseCoordinatesFromCenter();
        if (!targetCoordinates.IsValid(EntManager))
            return;

        string? capturedName = null;
        if (TargetingMode == ShuttleNavTargetingMode.Autopilot &&
            TryGetTargetGrid(targetCoordinates, out var targetGrid) &&
            EntManager.TryGetComponent(targetGrid, out PhysicsComponent? targetBody))
        {
            targetCoordinates = new EntityCoordinates(targetGrid, targetBody.LocalCenter);
            capturedName = EntManager.GetComponent<MetaDataComponent>(targetGrid).EntityName;
        }
        else if (TargetingMode == ShuttleNavTargetingMode.Bss &&
                 TryGetTargetBeacon(targetCoordinates, out var targetBeacon))
        {
            targetCoordinates = EntManager.GetCoordinates(targetBeacon.Coordinates);
            capturedName = targetBeacon.Name;
        }

        var targetMapCoordinates = _transform.ToMapCoordinates(targetCoordinates);
        if (targetMapCoordinates.MapId != mapId)
            return;

        var targetColor = Color.LimeGreen;
        if (TargetingMode == ShuttleNavTargetingMode.Bss &&
            !_shuttles.FTLFree(ourGridId.Value, targetCoordinates, TargetAngle, _bssExclusions))
        {
            targetColor = Color.Magenta;
        }

        var targetPosition = Vector2.Transform(targetMapCoordinates.Position, worldToView);
        // The green NAV diamond marks the movable camera origin. Travel, however, starts at the
        // shuttle's physics centre, which is also what FTL range checks and autopilot steering use.
        if (!EntManager.TryGetComponent(ourGridId.Value, out PhysicsComponent? ourBody) ||
            !EntManager.TryGetComponent(ourGridId.Value, out TransformComponent? ourTransform))
        {
            return;
        }

        var shuttleMapPosition = Maps.GetGridPosition((ourGridId.Value, ourBody, ourTransform));
        var startPosition = Vector2.Transform(shuttleMapPosition, worldToView);

        handle.DrawDottedLine(startPosition, targetPosition, targetColor, (float) _timing.RealTime.TotalSeconds * 30f);
        handle.DrawCircle(targetPosition, 13f, targetColor.WithAlpha(0.08f));
        handle.DrawCircle(targetPosition, 13f, targetColor, filled: false);
        handle.DrawLine(targetPosition - Vector2.UnitX * 7f, targetPosition + Vector2.UnitX * 7f, targetColor);
        handle.DrawLine(targetPosition - Vector2.UnitY * 7f, targetPosition + Vector2.UnitY * 7f, targetColor);

        var direction = Vector2.TransformNormal(TargetAngle.RotateVec(new Vector2(0f, -1f)), worldToView);
        if (direction.LengthSquared() > 0.001f)
        {
            direction = Vector2.Normalize(direction) * 24f;
            var tip = targetPosition + direction;
            var normal = new Vector2(-direction.Y, direction.X) * 0.22f;
            handle.DrawLine(targetPosition, tip, targetColor);
            handle.DrawLine(tip, tip - direction * 0.35f + normal, targetColor);
            handle.DrawLine(tip, tip - direction * 0.35f - normal, targetColor);
        }

        if (!string.IsNullOrEmpty(capturedName))
            handle.DrawString(Font, targetPosition + new Vector2(15f, -18f) * UIScale, capturedName, 0.82f * UIScale, targetColor);
    }

    protected DetectionLevel GetGridDetected(EntityUid grid)
    {
        if (Detectors != null)
            return _detection.IsGridDetected(grid, Detectors);

        return _consoleEntity == null ? DetectionLevel.Undetected : _detection.IsGridDetected(grid, _consoleEntity.Value);
    }

    private (Vector2 Top, Vector2 Left, Vector2 Offset) GetSize(Box2Rotated bounds)
    {
        var top = (bounds.TopLeft + bounds.TopRight) * 0.5f;
        var left = (bounds.TopLeft + bounds.BottomLeft) * 0.5f;
        var offset = (bounds.TopRight + bounds.BottomLeft) * 0.5f;

        return (top - offset, left - offset, offset);
    }

    private void DrawCelestialNavigation(DrawingHandleScreen handle, Matrix3x2 worldToView)
    {
        _planetHitTargets.Clear();
        if (!_planetaryState.Available)
            return;

        var stellarPosition = Vector2.Transform(_planetaryState.StellarCenter, worldToView);
        var orbitingBodies = _planetaryState.Bodies.Where(body =>
            body.OrbitRadius > 0f && MathF.Abs(body.OrbitAngularSpeed) > float.Epsilon);

        foreach (var body in orbitingBodies)
        {
            var orbitRadius = body.OrbitRadius * MinimapScale;
            if (orbitRadius > 0f)
            {
                // A dark shoulder and a brighter inner trace keep the orbit readable over both the
                // tactical grid and the animated celestial shaders without turning it into a halo.
                handle.DrawCircle(
                    stellarPosition,
                    orbitRadius + UIScale,
                    KoronusOrbitShadowColor,
                    filled: false);
                handle.DrawCircle(stellarPosition, orbitRadius, KoronusOrbitColor, filled: false);
            }
        }

        var star = _planetaryState.Bodies.FirstOrDefault(body => body.BodyType == "Star");
        var sunRadius = star?.NavVisualRadius * MinimapScale ?? 0f;
        if (sunRadius > 0f)
            _sunRenderer.Draw(handle, stellarPosition, sunRadius);

        var phaseTime = (float) IoCManager.Resolve<IGameTiming>().CurTime.TotalSeconds;
        foreach (var body in _planetaryState.Bodies)
        {
            if (body.OrbitRadius <= 0f)
                continue;

            var position = GetCelestialPosition(body, phaseTime);
            var viewPosition = Vector2.Transform(position, worldToView);
            var radius = body.NavVisualRadius * MinimapScale;
            var color = GetCelestialColor(body);

            if (radius <= 0f)
                continue;

            handle.DrawCircle(viewPosition, radius + 3f * UIScale, Color.Black.WithAlpha(0.75f));
            var renderedAsPlanet = false;
            if ((body.BodyType is "Planet" or "Moon" or "Asteroid") &&
                _prototypeManager.TryIndex<KoronusCelestialBodyPrototype>(body.Id, out var bodyPrototype))
            {
                renderedAsPlanet = _planetRenderer.Draw(
                    handle,
                    viewPosition,
                    radius,
                    bodyPrototype,
                    stellarPosition - viewPosition);
            }

            if (!renderedAsPlanet)
                handle.DrawCircle(viewPosition, radius, color);

            handle.DrawCircle(viewPosition, radius + 1.5f * UIScale, Color.White.WithAlpha(0.55f), filled: false);

            if (viewPosition.X >= -radius * 2f &&
                viewPosition.X <= PixelWidth + radius * 2f &&
                viewPosition.Y >= -radius * 2f &&
                viewPosition.Y <= PixelHeight + radius * 2f)
            {
                var label = body.Name;
                var labelSize = handle.GetDimensions(Font, label, 0.6f * UIScale);
                handle.DrawString(
                    Font,
                    viewPosition + new Vector2(-labelSize.X * 0.5f, radius + 4f * UIScale),
                    label,
                    0.6f * UIScale,
                    Color.White.WithAlpha(0.84f));
            }

            var visualRadius = renderedAsPlanet ? radius * 1.25f : radius;
            var hitRadius = MathF.Max(visualRadius, 8f * UIScale) + 6f * UIScale;
            _planetHitTargets.Add(new PlanetHitTarget(body, viewPosition, hitRadius, visualRadius));
        }
    }

    private bool TrySelectPlanet(Vector2 position)
    {
        foreach (var target in _planetHitTargets)
        {
            if (Vector2.DistanceSquared(position, target.Position) > target.HitRadius * target.HitRadius)
                continue;

            PlanetSelected?.Invoke(target.Body);
            return true;
        }

        return false;
    }

    private Vector2 GetCelestialPosition(KoronusCelestialBodyState body, float time)
    {
        var phase = (body.OrbitPhase + body.OrbitAngularSpeed * time) * MathF.PI / 180f;
        return _planetaryState.StellarCenter + new Vector2(MathF.Cos(phase), MathF.Sin(phase)) * body.OrbitRadius;
    }

    private static Color GetCelestialColor(KoronusCelestialBodyState body)
